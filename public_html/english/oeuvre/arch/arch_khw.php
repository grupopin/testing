  	<h2>KunstHausWien</h2>
        <h3>1989-1991</h3>
  	    <p><i>Untere Wei&szlig;gerberstra&szlig;e 13, 1030 Vienna, Austria</i>
  	    <br><i>Tel. 712 04 95</i>
  	    <br><a href="http://www.kunsthauswien.com/" target="_blank">www.kunsthauswien.com</a>
  	    </p>

	<div class="newsItem">
	<p>
	<a href="javascript:showPic(1)"><img src="images/khw/arch_0068_8_sc_sml.jpg" width="164" height="171" alt="" /></a>
  <a href="javascript:showPic(0)"><img src="images/khw/arch_0068_khw_vorzustand_sml.jpg" width="120" height="170" alt="" /></a>
  </p>
	<p>Suitable premises for a permanent exhibition of Hundertwasser's  paintings were found in the third district of Vienna, the building of  the former furniture factory of the Thonet Brothers, built in 1892. </p>
	</div>
	<div class="newsItem alternative">	
	<p><img src="images/khw/baustelle_small.jpg" width="144" height="190" alt="" /></p>
	<p>The  size of the building made it possible not only to establish a  Hundertwasser museum, but also to include rooms for alternating  exhibitions of international stature in the planning.The rebuilding,  which involved integrating two buildings, was carried out from 1989 to  1991, with the Bank for Labour and Business (Bank für Arbeit und  Wirtschaft), under its General Manager Walter Flöttl, acting as  building sponsor.</p>
	</div>
	
	<div class="newsItem"> 
	<p><img src="images/khw/baustelle2_small.jpg" width="162" height="210" alt="" /></p>
	<p>Hundertwasser was careful to leave the building's fabric untouched as  far as possible. Towards the street, an extension was added whose front  bay windows provide additional light to the exhibition rooms; on the  courtyard side a glass-walled stairwell was added.</p>
	<p>On April 9, 1991 KunstHausWien was officially opened; it is managed as  a private business, without any public funding or tax money. The total  exhibition space comes to about 4,000 sq.m. On the ground floor there  are the Museum Shop and a café/restaurant, apart from the cashier's and  coat-check booths. On the first two upper floors a cross-section of  Hundertwasser's uvre is on display, including painting, graphics,  tapestries and architectural models. The third and fourth floors are  reserved for the international exhibitions.</p>
	</div>
	

