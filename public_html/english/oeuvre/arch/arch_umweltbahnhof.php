  		<h2>Environmental Railway Station, Uelzen</h2>
		<h3>1999-2000</h3>
        <p><i>Uelzen, Germany</i></p>
		<p><a href="javascript:showPic(2)"><img src="images/hundertwasser/architektur/umweltbahnhof/umweltbahnhof.jpg" alt="Umweltbahnhof" width="47" height="120" class="inlineimg" style="margin-top:3px;"/></a><a href="javascript:showPic(3)"><img src="images/hundertwasser/architektur/umweltbahnhof/umweltbahnhof_modell-thumb.jpg" alt="Umweltbahnhof" width="120" height="66" class="inlineimg" style="margin-top:3px;"/></a>The interparty &quot;Working Group EXPO 2000&quot;, which was established within  the parliament of the state of Lower Saxony, approached Hundertwasser  in 1995 to request that he redesign the 1887 dated Wilhelminian train  station that had been disfigured by later additions made to the  building.</p>
		<p>&nbsp;</p>
		<p>Hundertwasser was, however, only awarded the commission after repeated changes in the personnel      responsible for the project at the Deutsche Bahn AG, and years after contact had first been established. Hundertwasser's      conditionsfor redesigning the station included re-exposing the Wilhelminian facade and planting trees wherever      possible.</p>
