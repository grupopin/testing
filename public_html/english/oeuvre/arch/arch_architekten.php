	<div style="width: 700px; margin: 0; padding-left: 20px;">
	  <h2>Hundertwasser Architects</h2>
  </div>
  <div style="margin: 0; padding: 0px 220px 20px 20px; ">
    <p>Friends, such as the architects Peter Pelikan and Heinz Springmann,  were vital to the success and exemplary effectiveness of all  Hundertwasser architecture. Hundertwasser found congenial partners in  them who implemented his ideas, his objectives and his philosophy in  their plans. Hundertwasser admired their skill and knowledge. Other  members of Hundertwasser's &quot;architecture family&quot; are the model builders,  Alfred Schmid, a friend of many years, and Andreas Bodi and their teams.</p>
  </div>
      <div style="margin: 20px 0 0 0; padding: 0px 25px 20px 120px; ">
      <h3 align="left">Dipl.-Ing. Peter Pelikan </h3>
      <p>Studied architecture at the Technical University, Vienna<br />
Worked since 1972 for the City of Vienna in the Town Planning Department<br />
Since 1980 architectural realisation of and collaboration on Hundertwasser's architectural projects.<br />
Peter Pelikan was the artist's closest collaborator in the field of architecture.</p>
</div>
      <div style="margin: 20px 0 0 0; padding: 0px 220px 20px 80px; ">
      <h3>Dipl.-Ing. Heinz M. Springmann </h3>
      <p>Freelance architect<br />
Born in 1951 in Rielasingen, District of Constance, Germany<br />
Studied architecture at the Technical University, Stuttgart<br />
The main focus of his work in the field of architecture lies in town  planning, contemporary architecture and the preservation of historical  monuments, and since working with Hundertwasser above all architecture  and ecology. </p>
</div>
      <div style="margin: 20px 0 0 0; padding: 0px 25px 20px 250px; ">
      <h3>Alfred Schmid</h3>
      <p>Born in 1950 in Vienna, lives and works in Klosterneuburg near Vienna<br />
Studied architecture at the Technical University, Vienna<br />
Since 1975 studio for architectural models<br />
Since 1980 collaboration with Hundertwasser</p>
</div>
      <div style="margin: 20px 0 0 0; padding: 0px 220px 20px 60px; ">
      <h3>Dipl.-Ing. Andreas Bodi 
      </h3>
      <p>Born in 1963 in Mödling, Lower Austria,<br />
lives and works in Maria Enzersdorf near Vienna<br />
Studied geodetics at the Technical University, Vienna<br />
1984 beginning of model making activities<br />
Since 1986 collaboration with Hundertwasser<br />
Since 1986 independent sculptural works, shown in exhibitions since 1993</p>
