	<div class="newsItem">
  		<h2>Church of St. Barbara, Bärnbach</h2>
	    <h3>1987-1988</h3>
	    <p><i>8572 B&auml;rnbach, Austria</i></p>
		
		<p><a href="javascript:showPic(0)"><img src="images/hundertwasser/architektur/st_barbara/arch_0050_31_SC_sml.jpg" width="255" height="228" alt="" /></a></p>
        <p>On the initiative of Father Friedrich Zeck, Hundertwasser accepted the  task of redesigning the Church of St. Barbara in Bärnbach. His  condition for doing so - that the surroundings of the church be  included in the planning, was agreed to by the community thanks to the  efforts of Mayor Konrad Bergmann.</p>
        <div style="height: 20px;"></div>
        
        <p><a href="javascript:showPic(1)"><img src="images/hundertwasser/architektur/st_barbara/ARCH_0050_7_sml.jpg" width="140" height="100" alt="" class="inlineimg" /></a>With the design of twelve gates standing for all the great religions in  the world, Hundertwasser made a gesture of ecumenism, tolerance and  togetherness.</p>
        <p><a href="javascript:showPic(2)"><img src="images/hundertwasser/architektur/st_barbara/arch_0050_XI_1_sml.jpg" width="165" height="255" alt="" /></a></p>

	    <p>With his church design and in outstanding co-operation with the  architect Manfred Fuchsbichler, Hundertwasser has created a remarkable  landmark in the midst of an industrial area. For the design of the  interior, artists from the region were given commissions.</p>
               
        <p>Hundertwasser only designed the aureole behind the altar, the floor of  the presbyterium and the spiral of the window behind the baptismal  font. Hundertwasser assisted the congregation in raising the funds for  the project.</p>
   		
