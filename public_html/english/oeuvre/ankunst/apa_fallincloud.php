	<h2>FALL IN CLOUD - FALL IN FOG - FALL OUT</h2>
	
	<div class="newsItem alternative">  
	<p><a href="javascript:showPic(0)"><img src="images/hundertwasser/apa/apa_0158_sml.jpg" width="250" height="183" alt="" /></a></p>
	<p class="werk">Three-dimensional object, silk screen, 1979</p>
	
	<p>For the production of this object a total of 61 print  runs were necessary, comprising 29 silk screens using 13 colours, and 32 metal  embossings in 7 colours. The three-dimensional object is made up of five  perspex panels. Hundertwasser created a transparent city with shining colours  and sparkling metals. A panel with a green tint was added in the centre to  enhance the distance effect. </p>
	<p>The development of the object took from 1973-1979. Some  machines were especially constructed for this project, which was coordinated by  Alberto della Vecchia. The silkscreen printing was made by Claudio Barbato who  was Hundertwasser’s printer in Venice.  The object was produced in an edition of 999 copies.</p>
