    <h2>Hundertwasser&rsquo;s Book Design &ndash; A Selection</h2>
     
	<div class="newsItem">    
	<h3>Catalogues for the World Travelling Museum Exhibition Austria Presents Hundertwasser to the Continents, 1975</h3>
<p>Hundertwasser designed the layout of the catalogues for his world travelling museum exhibition which was shown from 1975 to 1983 in 27 countries and 43 museums on all 4 continents and which was experienced by 4,870,000 visitors. 
  For these catalogues Hundertwasser used for the first time the so-called gradation frames of the pages and the painting reproductions, in order to give each page a different and individual appearance. The catalogue appeared for each country with a different reproduction on the cover, individualised by Hundertwasser with calligraphy of the name of the museum and the year.
  The first 16 pages contained texts written by the respective museum directors, heads of state and art historians from the various host countries. Its total edition in the three languages German, English and French as from 1976 up to the end of the world travelling museum exhibition tour in 1983 was 450,000 copies.
</p>
	</div>
  
 	<div class="newsItem alternative">
 	<p><a href="javascript:showPic(0)"><img src="images/hundertwasser/apa/apa_0192_1_2_sml.jpg" alt="" /></a></p>
	<h3>Exhibition Catalogue: Hundertwasser is painting, 1979</h3>
	<p>Hundertwasser designed this catalogue on the occasion of a travelling exhibition of his new paintings in five galleries in New York, Tokyo, Hamburg, Oslo and Vienna from 1979 to 1981. In this catalogue, the paintings were reproduced for the first time proportional to their actual size. The width of the pages tapers off like a fan, so that different views of the spreads are created through overlapping when pages are turned.</p>
	</div>

	<div class="newsItem">
	<p><a href="javascript:showPic(1)"><img src="images/hundertwasser/apa/apa_0226_sml.jpg" alt="" /></a></p>
	<div style="padding:0px 25px 0 182px;">
	<h3>Beautiful Paths &ndash; Thoughts on Art and Life, 1983</h3>
	<p>3 Designs for book covers, 1983</p>
	<p>Design I was used for the cover of the book edited by Walter Schurian, Beautiful Paths &ndash; Thoughts on Art and Life, in which Hundertwasser&rsquo;s philosophy was published in a selection of his writings. The book appeared in 1983 at the Deutscher Taschenbuch Verlag (dtv), Munich.  A new and completed edition of this text book was published by Langen M&uuml;ller Verlag, Munich in 2004.</p>
	</div>
	
 	<div class="newsItem">
	<h3>Brockhaus Encyclopedia, 1989</h3>
	<p>Hundertwasser designed the pattern for the production of the linen for the book covers, as well as the network of lines applied on the encyclopedia&rsquo;s 24 volumes.</p>
	<p><a href="javascript:showPic(2)"><img src="images/hundertwasser/apa/apa_0246_sml.jpg" alt="" /></a></p>
  <p>When placing the various volumes next to each other, opened, back-to-back or one above the other, an image always results (&hellip;) In weaving the linen for the covers, only natural materials should be used. After a few trials, threads from a special mixture of cotton and linen proved to be best suited to implementing Hundertwasser&rsquo;s intentions as to structure and colour. Their natural variation in thickness of the linen gives the cover vitality; the black weft thread and the coloured warp threads cause constantly changing colour combinations. (&hellip;) A special flock coating with velour gave the lines (on the covers, editor&rsquo;s note) an embossed effect, like a relief. They connect at the edges of the covers, so that the overall design can be made out. The stamping of the aluminium-based metallic foil was likewise a very complex process: as there were neither tools, stamps nor patterns which measured up to Hundertwasser&rsquo;s highly expressive design intentions, they had to be specially cast in brass. Only then could the actual production of the inlays begin. All in all, over 50 steps were needed to complete the cover of one volume.
</p>
	<p>In addition to the design of the cover boards, Hundertwasser designed the title panels on the spines and the image arrangement of the complete set. (&hellip;) One letter of the name Brockhaus was highlighted in the sequence of the volumes, so that from a distance the individual letters form the whole name once again.</p>
	<p class="reference">translated from the brochure: Kunstwerk, Die Hundertwasser-Ausgabe der Brockhaus-Enzyklop&auml;die, n. p. 1989</p>
	</div>
	
 	<div class="newsItem">
	<p><a href="javascript:showPic(3)"><img src="images/hundertwasser/apa/apa_0195_sml.jpg" alt="" /></a></p>
	<h3>The Bible, 1995 </h3>
	<p>Hundertwasser designed this bible edition over a period of several years. It is illustrated with 82 works, 32 of which are collages that he created specifically for this edition. He influenced the entire production of this bible, including the format and type of paper selected. Following his design, the woven linen cover is different in each copy. Each bible has a different colour combination within the linen weave. In addition, the copies vary in the colours of the metal embossing. Each copy is unlike all others. The covers were mostly produced by hand. In order to guarantee this uniqueness, the most difficult bookbinding challenges were mastered.</p>
	</div>

 	<div class="newsItem alternative">
	<p><a href="javascript:showPic(4)"><img src="images/hundertwasser/apa/apa_0354.jpg" alt="" /></a></p>
	<h3>Hundertwasser Catalogue Raisonn&eacute;, 1997-2002</h3>
	<p>Cover and book design: Friedensreich Hundertwasser<br /><br />
    Text Vol. I: Wieland Schmied, Munich<br /><br />
    Catalogue Raisonn&eacute;, Vol. II: Andrea Christa F&uuml;rst, Vienna</p>
    
    <p>Hundertwasser designed the layout of the book as well as its decor and cover. Each page received Hundertwasser's individual design for the type area and follows his ideas concerning size and placement of the illustrations.
In addition, Hundertwasser chose five of his doodle drawings for the design of the slipcase and the separate volumes. Another five doodles by Hundertwasser were rendered into colour etchings with an edition of 2,000 each. One of these prints is contained in every copy of the catalogue raisonn&eacute;. 
What has resulted is a bibliophile publication reflecting Hundertwasser's work and life exactly as he intended. 
Before his departure from New Zealand in February 2000, Hundertwasser wrote comments for selected works in his &oelig;uvre. 
</p>
	</div>

