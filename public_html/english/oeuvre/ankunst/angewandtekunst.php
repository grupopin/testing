	  <h2>Applied Art</h2>

	<div style="float: left; width: 200px; margin: 0;">
	<p>For Hundertwasser, art was not to be limited by any framework. An artist&rsquo;s work should have an effect on all areas of life, be it on clothing or, through articles for everyday use, on daily life. Hundertwasser also created many objects intended to express his quest for beauty and for variety in all areas of life, but also his concern and commitment for the environment and for nature. </p>
	</div>
	
	<div style="float: left; width: 200px; margin: 20px 0 20px 20px;">
	<p><a href="javascript:showPic(10)"><img src="images/hundertwasser/apa/apa_0219_sml.jpg" width="180" height="180" alt="" style="margin-bottom: 10px;"/></a></p>
	<p>Hundertwasser worked repeatedly and in great detail in each available medium and technique. He applied and expanded technical possibilities in an artistic manner, and also invented new ones, experimented with them, developed them, and used them in his art.</p>
	</div>
	
	<div style="float: left; clear: right; width: 200px; margin-left: 20px;">
	<p>In accordance with his philosophy &ldquo;Beauty is a Panacea &ndash; Sch&ouml;nheit ist ein Allheilmittel&rdquo;, Hundertwasser wanted to restore beauty and romanticism to their place in everyday life. In a time of mediocrity, of aesthetic emptiness and the soullessness of prefabrication, he endeavoured to help the hidden human longing for variety and beauty in harmony with nature come into its own.</p>
	<!-- <p class="reference">from: Hundertwasser 1928-2000, Catalogue Raisonn&eacute;, Cologne, 2002, Vol. II</p> -->
