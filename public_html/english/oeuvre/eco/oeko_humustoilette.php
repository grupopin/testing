	  <h2>Humus toilet and the biological purification plant</h2>

 	<div class="newsItem alternative">
    <p><a href="javascript:showPic(0)"><img src="images/hundertwasser/oeko/apa_0196_sml.jpg" width="200" height="300" alt=""></a></p>
	  <p>Since 1975 Hundertwasser had been preoccupied with the humus toilet and  the possibilities of biological water purification, especially the  plant purification systems. In all his residences he used humus  toilets. He developed various construction plans for the humus toilet  and described their function and use. The humus toilet functions in the  house, even in living quarters. There is no odor, there are no flies if  care is taken to cover the refuse with damp humus. No sewage is needed,  no flushing with water, no ventilation duct, no chemicals, not even  electricity.</p>
    </div>

 	<div class="newsItem">
	  <p><a href="javascript:showPic(1)"><img src="images/hundertwasser/oeko/apa_0196_2_sml.jpg" width="200" height="280" alt=""></a></p>
	  <p>For urine and waste water from the household, Hundertwasser designed a  biological water-purification system using aquatic plants and put it  into practise in several versions (KunstHausWien). The plant  purification system can be used in bright and warm interiors using the  following plants: calla, cypress grass, waterhyacinth, papyrus,  scouring rush, spiderwort, alocasia, jointed twig-rush, climbing fig  etc. Outdoors, bulrushes, reeds, cattails, water mint and water flag,  etc., are used.</p>
  </div>


 	<div class="newsItem alternative">
    <p><a href="javascript:showPic(2)"><img src="images/hundertwasser/oeko/apa_0196_skizze_small.jpg" width="251" height="214" alt=""></a></p>
	  <p>The dirty water must flow through the root portion of the aquatic  plants and through the colonies of decomposing bacteria; in this way it  is purified naturally. The dirt is converted in part to vegetable  matter and in part to mineral sediment.</p>
  </div>
	
