	  <p>His painting technique is also his very personal affair. Hundertwasser likes best to use paints he has pulverised or prepared himself, which he applies without mixing. Similarly, he prepares the priming ground himself; for prime coating, paint mixture and varnish he has developed various recipes of his own, all of which are designed to guarantee a long life for his pictures. In many of his pictures he uses oil, tempera and watercolour techniques in one picture to achieve a contrasting effect between the matte and radiant parts of the picture.</p>
	  
    <p>Hundertwasser&rsquo;s painting has gained acceptance in many places &ndash; pictures of his hang in major public and private collections of Europe, America and Japan &ndash; but it has had no lasting influence on the artists of his generation or that succeeding it. Hundertwasser has remained the outsider he always was. He will probably go down in art history as one of those individualists who have always existed outside the main current in all epochs, who can only be fitted into the image of an age contrapuntally and from a distance. Today the position of his painting is singular and unparalleled. That defines its incomparable stature, but also the narrow confines of its effectiveness.</p>

	 <p>Excerpt from: Wieland Schmied, Hundertwasser and his Painting, in: 
Hundertwasser KunstHausWien, Cologne, 1999</p>
