  		<h2>Painting</h2>
  		
 	<div class="newsItem alternative">
 	    <p><a href="javascript:showPic(50)"><img src="images/hundertwasser/malerei/0104_5c_sml.jpg" width="135" height="174" alt="" /></a></p>
 	    <p>Hundertwasser painted wherever he was, at home, in nature and on the road, in caf&eacute;s and restaurants, on the train or on aeroplanes, in hotels or at the homes of friends or acquaintances he was visiting. He had no studio and did not paint at an easel, but instead spread the canvas or sheet of paper flat in front of him. </p>
      <div style="height: 25px;"></div>
      
      <div style="height: 105px;"><a href="javascript:showPic(51)"><img src="images/hundertwasser/malerei/0150_5c_sml.jpg" alt=""/ ></a></div>

 	    <p>When he was on the road it could happen that he folded the paper, painting only on the visible part of his work. He either took his paintings on trips, or he left them at his places of residence and continued to paint them as soon as he returned.</p>

	    <p>Hundertwasser made many of his paints himself. He painted with watercolours, in oil and with egg tempera, with shiny lacquers and ground earth. He used various paints in one painting and put them next to each other, so that they contrasted not only in their colour but also in their texture. The "chassis" for his paintings he usually made himself, and he almost always stretched his own canvas.</p>
  
