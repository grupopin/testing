 	<div class="newsItem">

	   <p>He experimented with many techniques and invented new ones. He painted on different types of paper, favouring used wrapping papers, which he often mounted onto various supports, such as wood fibreboards, hemp or linen.</p>

 	
	<p><a href="javascript:showPic(54)"><img src="images/hundertwasser/malerei/0816_4c_sml.jpg" alt=""></a></p>
	   <p>
        He painted on found materials, for example on pieces of plywood, which he fitted together, as shown in work <i>904 Pellestrina Wood</i>, or on a switchboard, as in <i>816 Switch Board</i>.</p>
<p>&nbsp;</p>

    <p><a href="javascript:showPic(55)"><img align="right" src="images/hundertwasser/malerei/0904_4c_sml.jpg" alt="" /></a>Almost invariably, Hundertwasser noted on the front or reverse of his paintings where and when he painted them. The technical information about the work and the date he noted on labels applied to the reverse.</p>

    <p class="reference">From: Hundertwasser 1928-2000, Catalogue Raisonn&eacute;, Cologne, 2002, Volume 2</p>
