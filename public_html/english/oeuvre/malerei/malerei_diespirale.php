	<h2>The Spiral Is The Symbol Of Life And Death</h2>
	
	<div class="newsItem alternative">
	<p><a href="javascript:showPic(0)"><img src="images/hundertwasser/malerei/0433_5c_sml.gif" alt=""></a></p>
	<p>This spiral lies at that very point where inanimate matter is transformed into life. </p>
	<p>I am convinced that the act of creation took place in form of a spiral. Our whole life proceeds in spirals.</p>
	<p>Our earth describes a spiral course. We move in circles, but we never come back to the same point. The circle is not closed. We only pass the same neighbourhood many times. It is characteristic of a spiral that it seems to be a circle but is not closed.</p>
	<p>The true spiral is not geometric but vegetative. It has swellings, becomes thinner and thicker and flows around obstacles who are in her way.</p>
	</div>
	<div class="newsItem">
	<p><a href="javascript:showPic(1)"><img src="images/hundertwasser/malerei/0937_5c_sml.gif" alt=""></a></p>
	<p>The spiral shows life and death in both directions. Starting from the center, the infinite small the spiral means birth and growth, but by getting bigger and bigger the spiral dilutes into the infinite space and dies off like waves who disappear in the calm waters.</p>
	<p>On the contrary if the spiral condenses from outer space life starts from the infinite big, the spiral becomes more and more powerful and concentrates into the infinitely small which cannot be measured by man because it is beyond our conception and we call it death.</p>
  </div>

