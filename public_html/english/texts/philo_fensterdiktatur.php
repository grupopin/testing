	<h2>Window Dictatorship and Window Rights</h2>
	
 	<div class="newsItem alternative">
 	
  	<p><a href="javascript:showPic(0)"><img src="images/hundertwasser/philo/hundertwasser034_3_small.jpg" alt="" /></a></p>		
<p>Some people say houses consist of walls. I say houses consist of windows.</p>
<p>When different houses stand next to each other in a street, all having  different window types, i.e., window races, for example an Art Nouveau  house with Art Nouveau windows next to a modern house with unadorned  square windows, followed in turn by a Baroque house with Baroque  windows, nobody minds.</p>
<p>But should the three window types of the three houses belong to one  house, it is seen as a violation of the racial segregation of windows.  Why? Each individual window has its own right to life.</p>


 	<p><a href="javascript:showPic(1)"><img src="images/hundertwasser/philo/hundertwasser034_2_small.jpg" alt="" /></a></p>

		<p>According to the prevailing code, however, if window races are mixed,  window apartheid is infringed. Everything is there: racial prejudice,  racial discrimination, racial policy, racial ideology, racial barriers,  with fateful impact of window apartheid on man. The apartheid of window  races must cease. For the repetition of identical windows next to each  other and above each other as in a grid system is a characteristic of  concentration camps. In the new architecture of satellite towns and in  new administration buildings, banks, hospitals and schools, the  levelling of windows is unbearable.</p>
  </div>
  
 	<div class="newsItem">
   	
   <p><a href="javascript:showPic(2)"><img src="images/hundertwasser/philo/hundertwasser036_small.jpg" alt=""/ ></a></p> 
   
  	    <p>Individuals are never identical and defend themselves against these  standardising dictates either passively or actively, depending on their  constitution. Thus either with alcohol and drug addiction, exodus from  the city, cleaning mania, television dependency, inexplicable physical  complaints, allergies, depressions and even suicide, or alternatively  with aggression, vandalism and crime.</p>
  	    <p>&nbsp;</p>
  	    <p><a href="javascript:showPic(3)"><img src="images/hundertwasser/philo/hundertwasser032_small.jpg" alt="" /></a></p>

	<h3>Window Right</h3>
	 <p>A person in a rented apartment must be able to lean out of his window  and scrape off the masonry within arm's reach. And he must be allowed  to take a long brush and paint everything outside within arm's reach.  So that it will be visible from afar to everyone in the street that  someone lives there who is different from the imprisoned, enslaved,  standardised man who lives next door.</p>
		<p class="reference">Hundertwasser, January 22, 1990</p>
