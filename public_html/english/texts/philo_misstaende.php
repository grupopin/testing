	  		<h2>There Are No Evils in Nature. <br/>
                There Are Only Evils of Man.</h2>

		<div class="newsItem alternative">
<p><a href="javascript:showPic(0)"><img src="images/hundertwasser/philo/okologie-gen.jpg" width="237" height="311" alt=""></a></p>
        <p>When man thinks he has to correct nature, it is an irreparable mistake every time. <br />
          A community should not consider it an honour how much spontaneous vegetation it destroys; it should rather be a point of honour for every community to protect as much of its natural landscape as possible.</p>
        <p>&nbsp;</p>
        <p>The brook, the river, the swamp, the riverside wetlands as they are, the way God created them, must be sacred and inviolable to us.</p>
        <p>&nbsp;</p>
        <p>Correcting a stream only has evil effects, which are expensive in the end: the lowering of water tables, the destruction of forests, the transformation of large areas into steppes, no regeneration of the water, which runs off too fast. The river wetlands can no longer fulfill their sponge-like function: the absorption of excess water and slow feedback in dry spells, like a good piggy bank in times of emergency.</p>
        <p>&nbsp;</p>
        <p>The regulated brook becomes a sewer. Fish die, and there are no fish in the brook because they cannot swim through the regulated channel. Floods, with all their devastating consequences, all the more after regulation. Because too much water runs off too quickly, converging in great quantity without any chance of being absorbed by the earth and the vegetation.</p>
        <p>&nbsp;</p>
        <p>Only a stream with a high waterline flowing irregularly can produce pure water, regulate the water household and conserve the fish and animal populations to the benefit of man and his agriculture. </p>
        <p>&nbsp;</p>
        <p>Now, almost too late, this age-old adage is being recognised and the courses of rivers and streams, which had been straightened in concrete channels, are being destroyed in order to restore the previous irregular state. What irony!<br />
          So why regulate a stream if you have to deregulate it afterwards?<br />
        </p>
        <p class="reference">Hundertwasser, May, 1990</p> 
