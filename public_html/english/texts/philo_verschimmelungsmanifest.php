	  	<h2>Mouldiness Manifesto against Rationalism in Architecture</h2>
	  	 <p class="reference">(1958/1959/1964)</p>
	  	 
	  	 <br><br>
	
	 	<div class="newsItem alternative">
	 	
	<p><a href="javascript:showPic(0)"><img src="images/hundertwasser/philo/verschimmelungs_1958seckau_sml.jpg" width="410" height="283" alt=""></a></p>

 	    <p>Painting and sculpture are now free, inasmuch as anyone may produce any  sort of creation and subsequently display it. In architecture, however,  this fundamental freedom, which must be regarded as a precondition for  any art, does not exist, for a person must first have a diploma in  order to build. Why?</p>
 	    <p>&nbsp;</p>
 	    <p>Everyone should be able to build, and as long as this freedom to build  does not exist, the present-day planned architecture cannot be  considered art at all. Our architecture has succumbed to the same  censorship as has painting in the Soviet Union. All that has been  achieved are detached and pitiable compromises by men of bad conscience  who work with straight-edged rulers.</p>
 	    <p>&nbsp;</p>
 	    <p>The individual's desire to build something should not be deterred!  Everyone should be able and have to build and thus be truly responsible  for the four walls in which he lives. And one must take the risk into  the bargain that such a fantastic structure might collapse later, and  one should not and must not shrink from human sacrifice which this new  mode of building demands, perhaps demands. We must at last put a stop  to having people move into their quarters like chickens and rabbits  into their coops.</p>
	 	</div>
  
<div class="newsItem alternative">
  	  <p>If such a fantastic structure built by the tenants themselves  collapses, it will usually creak beforehand, anyway, so that people  will be able to escape. But from then on the tenant will deal more  critically and more creatively with the housing he lives in and will  bolster the walls and beams with his own hands if they seem too fragile  to him.</p>
  	  <p>&nbsp;</p>
  	  <p>The tangible and material uninhabitability of slums is preferable to  the moral uninhabitability of utilitarian, functional architecture. In  the so-called slums only the human body can be oppressed, but in our  modern functional architecture, allegedly constructed for the human  being, man's soul is perishing, oppressed. We should instead adopt as  the starting point for improvement the slum principle, that is, wildly  luxuriantly growing architecture, not functional architecture.</p>
  	  <p>&nbsp;</p>
  	  <p>Functional architecture has proved to be the wrong road to take,  similar to painting with a straight-edged ruler. With giant steps we  are approaching impractical, unusable and ultimately uninhabitable  architecture.</p>
  	  <p>&nbsp;</p>
  	  <p>The great turning point in painting the absolute tachistic automatism  is in architecture its absolute uninhabitability, which has yet to  come, because architecture lags thirty years behind.</p>
  	  <p>&nbsp;</p>
  	  <p>Just as today, after crossing beyond total tachistic automatism, we are  already witnessing the miracle of transautomatism, we will only  experience the miracle of a new, true and free architecture after  overcoming total uninhabitability and after a creative moulding  process. However, since we do not yet have total uninhabitability  behind us, as we are unfortunately not yet in the transautomatism of  architecture, we must first strive to achieve total uninhabitability,  the creative moulding in architecture, as quickly as possible.</p>
  	  <p>&nbsp;</p>
  	  </div>

<div align="center">
	<p><a href="javascript:showPic(1)"><img src="images/hundertwasser/philo/verschim_manifest_4_small.jpg" width="350" height="267" alt=""></a></p>
</div>
<div class="newsItem">
	<p>The apartment-house tenant must have the freedom to lean out of his  window and as far as his arms can reach transform the exterior of his  dwelling space. And he must be allowed to take a long brush and as far  as his arms can reach paint everything pink, so that from far away,  from the street, everyone can see: there lives a man who distinguishes  himself from his neighbours, the pent-up livestock! He must also be  allowed to cut up the walls and make all kinds of changes, even if this  disturbs the architectural harmony of a so-called masterwork, and he  must be able to fill his room with mud or children's modelling clay.</p>
	<p>&nbsp;</p>
	<p>But the lease prohibits this!</p>
	<p>&nbsp;</p>
	<p>The time has come for people to rebel against their confinement in  cubical constructions like chickens or rabbits in cages, a confinement  which is alien to human nature. Such a cage or utility construction is  a building alien to the nature of all three groups of men having to do  with it:</p>
  	
    	<p><a href="javascript:showPic(2)"><img src="images/hundertwasser/philo/verschim_manifest_2_small.jpg" width="241" height="350" alt=""></a></p>

      <p><b>+</b>The architect has no relationship to the building. Even the greatest  architectural genius cannot foresee what kind of being will dwell in  the house. The so-called human dimension and proportions in  architecture are a criminal fraud, especially when measurements are  based on the average value of a Gallup poll.</p>
  	  <ul>
  	    <li>The bricklayer has no relationship to the building. If, for example, he  wants to vary, if only slightly, the construction of a wall according  to his own moral and aesthetic concepts if he has any he loses his job.  And besides, he doesn't care, since he will not be living in the  structure.</li>
        <li></li>
        <li>The tenant has no relationship to the structure. He has not built it,  but only moved into it. His human needs, his human space, are probably  completely different. And this remains the case even if the architect  and the bricklayer try to build according to the instructions of the  future tenant and the building sponsor.</li>
  	    <li>Only when architect, bricklayer and tenant are a unity, or one and the  same person, can we speak of architecture. Everything else is not  architecture, but a criminal act which has taken on form.  Architect/bricklayer/tenant is a trinity just like the Holy Trinity of  God-Father/Son/Holy Spirit. The great similarity, almost identity of  these two trinities should be noted. If this unity,  architect/bricklayer/tenant is lost, there can be no architecture, just  as the current manufactured constructions should not be considered  architecture. Man has to regain the critical, creative function he has  lost and without which he ceases to exist as a human being!<b>+</b></li>
      </ul>
  	  <p>
  	    <b>+</b>Also criminal is the use of ruler and T-square in architecture, which,  as can be easily proved, have become instruments of the breakdown of  the architectural trinity.<br />
Just carrying a ruler with you in your  pocket should be forbidden, at least on a moral basis. The ruler is the  symbol of the new illiteracy. The ruler is the symptom of the new  disease, disintegration of our civilisation.</p>
</div>

<div class="newsItem">
	  <p>&nbsp;</p>

  	  <p>Today we live in a chaos of straight lines, in a jungle of straight  lines. If you do not believe this, take the trouble to count the  straight lines which surround you. Then you will understand, for you  will never finish counting.<b>+</b></p>
  	
    	<p><a href="javascript:showPic(3)"><img src="images/hundertwasser/philo/verschim_manifest_1_small.jpg" width="217" height="350" alt=""></a></p>

      <p><b>+</b>On one razor blade I counted 546 straight lines. By imagining linear  connections to another razor blade of the same manufacturing process,  which surely looks exactly the same, this yields 1,090 straight lines,  and adding on the packaging, the result is about 3,000 straight lines  from the same blade.<b>+</b></p>
      <p>&nbsp;</p>
      <p>Not all that long ago, possession of the straight line was a privilege  of royalty, the wealthy, and the clever. Today every idiot carries  millions of straight lines around in his pants pockets.</p>
  	  </div>

<div class="newsItem alternative">
	<p>This jungle of straight lines, which is entangling us more and more  like inmates in a prison, must be cleared. Until now, man has always  cleared away the jungles he was in and freed himself. But to clear a  jungle you must first become aware that you are in one, for this jungle  took form stealthily, unnoticed by mankind. And this time it is a  jungle of straight lines.</p>
	<p>&nbsp;</p>
	<p>Any modern architecture in which the straight line or the geometric  circle have been employed for only a second &ndash; and were it only in  spirit &ndash; must be rejected. Not to mention the design, drawing-board and  model-building work which has become not only pathologically sterile,  but absurd. The straight line is godless and immoral. The straight line  is not a creative line, it is a duplicating line, an imitating line. In  it, God and the human spirit are less at home than the comfort-craving,  brainless intoxicated and unformed masses.</p>
  	  </div>
  	  
<div class="newsItem">
  	  <p>Consequently, T-square structures, be they ever so curved, bending,  overhanging, or perforated, are invalid. This is all just the panic of  the constructive architects not to lose contact with trends and to  change their style in time. (Look at the Postmodernist style twenty  years later.)</p>
  	  <p>&nbsp;</p>
  	  <p>When rust sets in on a razor blade, when a wall starts to get mouldy,  when moss grows in a corner of a room, rounding its geometric angles,  we should be glad because, together with the microbes and fungi, life  is moving into the house and through this process we can more  consciously become witnesses of architectural changes from which we  have much to learn.</p>
  </div>
  
	<div class="newsItem alternative">
      <p>The irresponsible vandalism of the constructive, functional architects  is well known. They simply wanted to tear down the beautiful  stucco-fa&amp;ccdil;ade houses of the 1890s and Art Nouveau and put up  their own empty structures. Take Le Corbusier, who wanted to level  Paris completely in order to erect his straight-line, monstrous  constructions. Now, in the name of justice, the constructions of Mies  van der Rohe, Neutra, the Bauhaus, Gropius, Johnson, Le Corbusier, Loos  etc. should be torn down, as they have been outdated for a generation  and have become morally unbearable.</p>
      <p>&nbsp;</p>
      <p><a href="javascript:void(0);"><img src="images/hundertwasser/philo/verschimmelungs_cover_sml.jpg" width="226" height="320" alt="" /></a>However, the transautomatists, and everyone who is at a point beyond  uninhabitable architecture, deal with their predecessors more humanely.  They don't want any more destruction. In order to rescue functional  architecture from its moral ruin, a decomposing solution should be  poured over all those glass walls and smooth concrete surfaces, so the  moulding process can set in.</p>
      <p>&nbsp;</p>
      <p>It is time for industry to recognise its fundamental mission, which is  to engage in creative moulding! It is now the task of industry to  engender in its specialists, engineers and doctors a feeling of moral  responsibility towards moulding. This moral responsibility towards  creative moulding and critical weathering must already be established  in education laws. Only the engineers and scientists who are capable of  living in mould and producing mould creatively will be the masters of  tomorrow. (Last four paragraphs: addition by Pierre Restany, 1958) And  only after creative moulding, from which we have much to learn, will a  new and wonderful architecture come about.</p>
      <p>&nbsp;</p>
      <p><i>Die mit + eingefa&szlig;ten Zus&auml;tze sind erst nach dem Vortrag auf der Seckauer Tagung dem Verschimmelungsmanifest hinzugef&uuml;gt worden.</i></p>
  	  <p><i>++ Diese Abs&auml;tze sind eine Einf&uuml;gung von Pierre Restany, 1958</i></p>
  </div>
  	  
<div class="newsItem alternative">
	<p>&nbsp;</p>
  	  <h3>Addendum  1959</h3>
  	  <p>Today's architecture is criminally sterile. For unfortunately, all  building activity ceases at the very moment when man &quot;takes up  quarters&quot;, but normally building activity should not begin until man  moves in. We are outrageously robbed of our humanity by defiling  dictates and criminally forced not to make any changes or additions to  fa&amp;ccdil;ades, the layout or interiors, either in colour,  structure, or masonry. Even tenant-owned dwellings are subject to  censorship (see building-inspection regulations and lease statutes).  The characteristic thing about prisons, cages or pens is the  prefabricated &quot;a-priori&quot; structure, the definitive termination of  building activity prior to the prisoner&rsquo;s or animal&rsquo;s moving in to a  structure which is innately incompatible to him or it, coupled with the  categorical restriction that the inmate may change nothing in this  &quot;his&quot; housing, which has been imposed upon him.</p>
  	  <p>&nbsp;</p>
  	  <p><a href="javascript:showPic(4)"><img src="images/hundertwasser/philo/verschim_manifest_3_small.jpg" width="251" height="192" alt=""></a></p>

      <p>For true architecture grows out of normal building activity, and this  normal building activity is the organic development of a shell around a  group of people. Such building growth is like the growth of a child and  of man. Absolute completion of building construction is tolerable, if  at all, only in monuments and uninhabited architecture.</p>
      <p>&nbsp;</p>
      <p>But if a structure is intended to house people inside it, the  discontinuation of construction prior to habitation must be seen as an  unnatural sterilisation of the growing process and as such as a  criminal act which should be punished.</p>
      <p>&nbsp;</p>
      <p>The architect as we know him today is only entitled to construct  uninhabitable architecture, if he is indeed capable of doing so.  Habitable architecture is not his responsibility, and he must be  vehemently denied the right, just as society does not leave a notorious  poisoner or a mass murderer free to his devices.</p>
      <p>&nbsp;</p>
      <p>For the currently highly praised architectural pre-planning of  dwellings is little more than planned mass murder by premeditated  sterilisation. To prove this accusation, you just have to walk through  any recently constructed European town or suburb. To give just an idea of some exemplary, healthful contemporary architecture, and this list is, unfortunately, shamefully short:</p>
  	  <ol>
  	    <li>The Gaud&iacute; buildings in Barcelona.</li>
        <li>Certain Art Nouveau buildings.</li>
  	    <li>The Watts Tower by Simon Rodia, in a residential section of Los Angeles.</li>
  	    <li>Le Palais Du Facteur Cheval in the D&eacute;partement de la Dr&ocirc;me, France.</li>
  	    <li>The slum sections of cities, the so-called &quot;urban blemish&quot; (&quot;taudis&quot; in French, sections &quot;insalubres&quot;).</li>
  	    <li>Homes of peasants and primitives, whenever still handmade, as earlier.</li>
  	    <li>Old Austrian and German &quot;schreberg&auml;rten&quot; (workers&rsquo; allotment-garden houses).</li>
  	    <li><i>Illegally built American self-made houses.</i>*</li>
  	    <li><i>Dutch and Sausalito houseboats.</i>*</li>
  	    <li> Buildings by the architects Christian Hunziker, <i>Lucien Kroll</i> and a few others</li>
  	  </ol>
  	  <p>* In <i>italics</i> Hundertwassers revisions and addenda from 1996.</p>
  </div>

  
<div class="newsItem">
    <h3>Addendum  1964</h3>
      <p>The architect&rsquo; only function should be that of technical advisor, i.e.,  answering questions regarding materials, stability, etc. The architect  should be subordinate to the occupant (tenant, owner, lodger) or at  least to the occupant&rsquo;s wishes.<br />
All occupants must be free to  create their &quot;outer skins&quot; &ndash; they must be free to determine and  transform the outward shell of their domicile facing the street.</p>
