  <div id="content" class="wide">
  		<h2>Hundertwasser on Hundertwasser</h2>
  		
		<div class="newsItem"> 
		<p><a href="javascript:showPic(0);"><img src="/images/hundertwasser/fruh-p1.jpg" width="92" height="104" alt=""></a></p>
 		<p>What does a man need in order to be happy.</p>
    	<p>Progression is retrogression and retrogression becomes progression.</p>

    	<p>My painting is, I think, completely different because it is vegetative painting.</p>
  		<p>One reason why other people do not want to paint
vegetatively or want to take to a vegetative way of
life is because it begins too unpretentiously, it does
not have great eclat or drum roll; on the contrary it
grows quite slowly and simply, and that does not
appeal to our social order, people want instant
results based on the slash and burn principle.</p>
    	<p>I should like, and I do it too quite instinctively,
to live an example, live an example to people, paint
for them a paradise that each may have, he need
only grasp it.</p>
      <p>Paradise is there, but we destroy it.</p>
    	<p><a href="javascript:showPic(1);"><img src="/images/hundertwasser/fruh-p2.jpg" width="91" height="113" alt=""></a></p>
  		<p>I want to show how basically simple it is to have paradise on earth.</p>

  		<p>And everything that the religions and dogmas and the various political creeds promise, is all nonsense.</p>
 	</div>
	
		<div class="newsItem"> 
		<p>And there of course I come into conflict with
society which completely misunderstands that.</p>
  	<p>They believe that it is eccentricity, just a
publicity stunt, but they forget that that is part of
myself, that that is my natural form of expression.</p>
  	<p>Why may a human being not do what he needs to do, like a flower.</p>
  	<p>The colourful, the abundant, the manifold, is always better than mediocre grey and uniformity.</p>

  	<p>Only those who think and live creatively will survive in this life and beyond.</p>
	</div>
	
		<div class="newsItem alternative"> 
		<p><a href="javascript:showPic(2);"><img src="/images/hundertwasser/fruh-p3.jpg" width="94" height="120" alt=""></a></p>
		<p>One must live as though one were at war and everything rationed</p>
		<p>Man must be careful</p>
		<p>Must think independently, must economize</p>

		<p>Should not waste blindly</p>
		<p>Man must take care that the cycle functions</p>
		<p>The cycle from eating to shitting functions naturally</p>
		<p>But the cycle from shitting to eating is disconnected</p>
		<p>Being happy does not depend on wealth at all</p>
		<p>Does not depend on production</p>

		<p>That is difficult to say.</p>
	</div>
	
		<div class="newsItem"> 
    <p><a href="javascript:showPic(3);"><img src="/images/hundertwasser/fruh-p4.jpg" width="96" height="103" alt=""></a></p>
		<p>Paintings for me are gateways, which enable me, if I have <br />
		  been successful, to open them into a world which is both<br />
		  near and far for us, to which we have no admission, in<br />

		  which we find ourselves, but which we cannot perceive,<br />
		  which is against the real world</p>
		<p>Our parallel world, from which we remove ourselves in one respect</p>
    <p>Yes, and that is the paradise, that is what we are in,
what we are arrested in, and which some inexplicable power denies us.</p>
		<p>And so I have succeeded in throwing windows open</p>
		<p>How I succeeded is difficult to explain</p>

		<p>On no account by force, nor by calculation, nor by
intelligence, nor necessarily by intuition, but almost
as though sleep-walking.</p>
	</div>
	
		<div class="newsItem alternative"> 
		<p><a href="javascript:showPic(4)"><img src="/images/hundertwasser/reife-p1.jpg" width="88" height="95" alt=""></a></p>
		<p>The work of the artist is very difficult, because it
cannot be done by force, diligence or intelligence</p>
    <p>I think that by strength and diligence and intelligence
one can do anything else in life, but the rewards of 
art are totally unattainable by these means</p>
    <p>Therefore, by goodness even a good person, finds
himself suddenly up against a barrier, he cannot get beyond it</p>

    <p>It is very strange, isn’t it, if a man contributes all
he has, diligence, goodness, perseverance, intelligence, everything that he has, and in spite of that he
doesn’t get anywhere</p>
    <p>What is the reason for this.</p>
    <p>I believe, and I am absolutely certain, and therefore
I believe, that painting is a religious occupation,
that the actual impulse comes from without, from
something else that we do not know, an indefinable
power which comes or does not come and which guides
your hand</p>
    
    <p>People used to say in earlier times that it was the
muse, for example, it's a stupid thing to say of course,
but it is some kind of illumination</p>
    
    <p>And the only thing one can do is to prepare the
ground, so that this extraterrestrial impulse or
however else one might describe it can reach you.</p>
    </div>

		<div class="newsItem">
    <p><a href="javascript:showPic(5);"><img src="/images/hundertwasser/reife-p3.jpg" width="102" height="107" alt=""></a></p>
    <p>That means keeping oneself ready</p>
    
    <p>That means eliminating the will, eliminating the
intelligence, eliminating "wanting to do better",
eliminating ambition</p>
    
    <p>I should perhaps like to be known as the magician of
vegetation or something similar. We are in need of magic<br/>
		  I fill a picture until it is full with magic, as one
fills up a glass with water</p>
    
    <p>Everything is so infinitely simple, so infinitely beautiful.</p>

    
    <p style="padding: 5px; font-style: italic;">Venice, 1975</p>
	</div>
	
