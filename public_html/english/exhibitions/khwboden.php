	<div id="content">
  		<h2>The Uneven Floor</h2>

	<div class="newsItem">
	<p><a href="javascript:showPic(0)"><img src="images/khw/khw_wellboden_1.jpg" alt=""></a></p>
	<p>
	The flat floor is an invention of the architects. It fits engines  -  not human beings.</p>
	<p>People not only have eyes to enjoy the beauty they see and ears to hear melodies and noses to smell nice scents. People also have a sense of touch in their hands and feet.</p>
	<p>
	If modern man is forced to walk on flat asphalt and concrete floors as they were planned thoughtlessely in designers' offices, estranged from man's age-old relationship and contact to earth, a crucial part of man withers and dies. This has catastrophic consequences for the soul, the equilibrium, the well being and the health of man. Man forgets how to experience things and becomes emotionally ill.
	</p>
	<p><a href="javascript:showPic(1)"><img src="images/khw/khw_3haut_2.jpg" alt=""></a></p>
	<p>An uneven and  animated floor is the recovery of man’s mental equilibrium,of the dignity of  man which has been violated in our levelling, unnatural and hostile urban grid  system.</p>
	<p>The uneven floor becomes a symphony, a melody for the feet and brings back natural vibrations to man.</p>
	<p>
	<a href="javascript:showPic(2)"><img src="interface/white/gr8.png" alt="image" width="78" height="83" /></a> Architecture  should elevate and not subdue man. It is good to walk on uneven floors and  regain our human balance. </p>
	<p>
	<em>Hundertwasser, April, 1991</em>
	</p>
		
