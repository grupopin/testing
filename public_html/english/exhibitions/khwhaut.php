	<div id="content">
	<h2>The Third Skin in the Third District</h2>

	<div class="newsItem">
	<p><a href="javascript:showPic(0)"><img src="images/khw/arch_0068_6_sml.jpg" alt=""></a></p>
	<p>
	This is about the third skin, the outer skin of KunstHausWien.
	</p>
	<p>
	Man is surrounded by three layers, his skin, his clothing and walls, the building. Clothing and the walls of buildings have in recent times undergone a development which is no longer in keeping with the individual's natural requirements.
	</p>
	<p>
	The windows are an equivalent of the eyes.
	</p>
	
	<p>
The facade is not perfectly straight and flat, but humpy and interrupted by irregular mosaics. A black-and-white, irregular checkerboard pattern signals the disbanding of the grid system, its break-up.
	</p>
	</div>
	
	<div class="newsItem alternative">
	<p>
	<a href="javascript:showPic(1)"><img src="images/khw/hw_im_khw_2.jpg" alt="image" width="73" height="103" /></a>On the side toward the courtyard a black stairwell connects the exhibition rooms.
	</p>
	<p>
	The entrance and courtyard were paved with old granite blocks, Dutch clinkers and old bricks from Emperor Franz Joseph's days  -  a chance for the craftsmen to be creative themselves, a joy for the visitors.
	</p>
	<p>
	What we urgently need are barriers of beauty; these barriers of beauty consist of uncontrolled irregularities. 
	</p>
	<p>
	<p>
	Paradises can only be made with our own hands, with our own creativity in harmony with the free creativity of nature.
	</p>
	<em>Hundertwasser, April, 1991</em>
	</p>
	</div>
      
