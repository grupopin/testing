	  <h2>Angewandte Kunst</h2>

	<div style="float: left; width: 200px; margin: 0;">
	<p>F&uuml;r Hundertwasser sollte Kunst nicht von einem Rahmen begrenzt sein. Das Werk eines K&uuml;nstlers sollte in alle Lebens&shy;bereiche einwirken, sei es in die Mode oder &uuml;ber Gebrauchsgegenst&auml;nde in das t&auml;gliche Leben. Hundertwasser hat auch viele Objekte geschaffen, die seinem Streben nach dem Sch&ouml;nen, nach Vielfalt in allen Lebensbereichen, aber auch seinem Anliegen und seinem Einsatz f&uuml;r die Umwelt und f&uuml;r die Natur Ausdruck verleihen sollten.</p>
	</div>
	
	<div style="float: left; width: 200px; margin: 20px 0 20px 20px;">
	<p><a href="javascript:showPic(10)"><img src="images/hundertwasser/apa/apa_0219_sml.jpg" width="180" height="180" alt="" style="margin-bottom: 10px;"/></a></p>
	<p>Hundertwasser hat sich immer wieder und ausf&uuml;hrlich mit dem jeweils zur Verf&uuml;gung stehenden Medium und der Technik befa&szlig;t. Er hat technische M&ouml;glichkeiten k&uuml;nstlerisch genutzt und erweitert, dar&uuml;ber hinaus neue entdeckt, mit diesen experimentiert, sie entwickelt und in seiner Kunst eingesetzt.</p>
	</div>
	
	<div style="float: left; clear: right; width: 200px; margin-left: 20px;">
	<p>Hundertwasser wollte gem&auml;&szlig; seiner Philosophie von &quot;Beauty is a Panacea - Sch&ouml;nheit ist ein Allheilmittel&quot; der Sch&ouml;nheit und der Romantik wieder zu ihrem Platz im Alltag verhelfen. In einer Zeit des Mittelma&szlig;es, der &auml;sthetischen Leere und Seelenlosigkeit des Vorfabrizierten trachtete er danach, der verborgenen Sehnsucht der Menschen nach Vielfalt und nach Sch&ouml;nheit in Harmonie mit der Natur zu ihrem Recht zu verhelfen.</p>
	<!-- <p class="reference">Aus: Hundertwasser 1928-2000, <cite>Catalogue Raisonn&eacute;</cite>, K&ouml;ln 2002, Band 2 </p> -->
