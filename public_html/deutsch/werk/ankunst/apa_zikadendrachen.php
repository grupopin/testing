	  <h2>ZIKADENDRACHEN</h2>

	<div class="newsItem alternative">	    
	<p><a href="javascript:showPic(0)"><img src="images/hundertwasser/apa/apa_0306_07_sml.jpg" width="250" height="211" alt=""></a></p>
	<p class="werk">Dreidimensionale Objekte, 1989</p>
	
	<p>F&uuml;r die Anfertigung der Drachen wurden die Holzschnitte 557B <em>Do Not Wait Houses Move</em> und 870A <em>Pacific Raindrop on Tahiti </em>, geschnitten von Tatsuo Kawashima und gedruckt von Akio Shimizu, beide Uchida Moto Han in Kioto, verwendet. Die Drachen wurden von Masaaki Sato, Nagoya , gebaut. </p>
	</div>
	<div class="newsItem">
	<p><a href="javascript:showPic(1)"><img src="images/hundertwasser/apa/apa_0306_08_sml.jpg" width="240" height="201" alt=""></a></p>
	<p> Es wurden 12 in Form und Motiv unterschiedliche Varianten geschaffen, in einer Auflage von jeweils 75 St&uuml;ck. </p>
	<p>Die Anregung zu diesen Objekten ging vom Goethe-Institut in Osaka aus, das zum "Jahr des Drachen" (1988) 100 K&uuml;nstler aus 20 L&auml;ndern gebeten hatte, einen japanischen Drachen zu bemalen. Die Drachen wurden in der Wanderausstellung "Bilder f&uuml;r den Himmel" auf der ganzen Welt gezeigt. </p>
	</div>
	
	<div class="newsItem alternative">	
	<p><a href="javascript:showPic(2)"><img src="images/hundertwasser/apa/apa_0306_09_sml.jpg" width="250" height="213" alt=""></a></p>
	<p> Bei Hundertwassers Zikadendrachen handelt es sich um japanische Drachen in den traditionellen Formen der Insektendrachen, wie sie in der Region um Nagoya zu Hause sind. Der Tradition entsprechend ist der Zikadendrachen mit einem <em>unari- </em>Schwirrholz ausgestattet, dessen gespannte Sehne im Wind das typische Zirpen der Zikade erklingen l&auml;&szlig;t. Hundertwasser lie&szlig; sich dazu bewegen, eine Serie von Drachen zu gestalten, in der die zw&ouml;lf Formen aus dem Repertoire Masaaki Satos enthalten sind, darunter Zikaden, Bienen, Hummeln, Pferdefliegen und der runde Wanwan-Drachen. </p>
	
	</div>
