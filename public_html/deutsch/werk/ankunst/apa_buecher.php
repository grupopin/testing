    <h2>Buchgestaltungen - Eine Auswahl</h2>
     
	<div class="newsItem">    
	<h3>Kataloge der Welt-Wander-Museumsausstellung<br/> 
	&Ouml;sterreich zeigt den Kontinenten Hundertwasser, 1975</h3>
	<p>Hundertwasser gestaltete das Layout der Kataloge f&uuml;r die Welt-Wander-Museumsausstellung, die von 1975 bis 1983 in 27 L&auml;ndern und 43 Museen auf 4 Kontinenten gezeigt und von 4.870.000 Besuchern erlebt wurde.<br/>
	Er verwendete in den Katalogen erstmals die sogenannte Schummerung f&uuml;r die Seiten und Gem&auml;lde-Abbildungen, so da&szlig; jede Seite individuell verschieden ist.</p>
	<p>Der Katalog erschien zur Weltwanderausstellung in jedem Land mit einer anderen Abbildung auf dem Einband und dem von Hundertwasser handgeschriebenen Namen des Museums und des Jahres. Die ersten 16 Seiten enthielten Texte von Museumsdirektoren, Staatsoberh&auml;uptern und Kunsthistorikern der jeweiligen Gastl&auml;nder. Die Gesamtauflage in allen drei Sprachen Deutsch, Englisch und Franz&ouml;sisch betrug von 1975 bis zum Jahr 1983 450.000 Exemplare.</p>
	</div>
  
 
