	<h3>Ausstellungskatalog: <br/>
	Hundertwasser Is Painting, 1979</h3>
	<p>Hundertwasser gestaltete diesen Katalog anl&auml;&szlig;lich einer Ausstellungstournee seiner Bilder in Galerien in New York, Tokio, Hamburg, Oslo und Wien in den Jahren 1979-1981. Die Bilder wurden im Verh&auml;ltnis ihrer tats&auml;chlichen Gr&ouml;&szlig;e reproduziert. Die Breite der Seiten verj&uuml;ngt sich f&auml;cherartig, so da&szlig; sich beim Bl&auml;ttern durch &Uuml;berschneidungen jeweils unterschiedliche Ansichten der Doppelseite ergeben. </p>
	</div>

