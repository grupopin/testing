	<h3>Sch&ouml;ne Wege<br/> 
	Gedanken &uuml;ber Kunst und Leben, 1983</h3>
	<p>3 Entw&uuml;rfe f&uuml;r einen Buchumschlag, 1983 </p>
	<p>Entwurf I wurde f&uuml;r das Cover des Buches verwendet:<br/>
	Schurian, Walter (Hg): <em>Hundertwasser - Sch&ouml;ne Wege, Gedanken &uuml;ber Kunst und Leben<br/>
	</em>mit Texten und Manifesten von Hundertwasser, erschienen im Deutschen Taschenbuch Verlag (dtv), M&uuml;nchen 1983. Eine erweiterte Neuausgabe dieses Textbuches erschien 2004 bei Langen M&uuml;ller Verlag, M&uuml;nchen 2004 </p>
	</div>
	
 	<div class="newsItem">
	<h3>Brockhaus Enzyklop&auml;die, 1989</h3>
	<p>Hundertwasser hat die Vorlage zur Herstellung des Leinens f&uuml;r die Buchdeckel sowie die Vernetzung der Linien der 24 B&auml;nde der Enzyklop&auml;die entworfen. </p>
	<p><a href="javascript:showPic(2)"><img src="images/hundertwasser/apa/apa_0246_sml.jpg" alt="" /></a></p>
  <p>Legt man die einzelnen B&auml;nde nebeneinander, aufgeschlagen, R&uuml;cken an R&uuml;cken oder &uuml;bereinander, so ergeben sie immer ein Bild. (...) beim Weben des Einbandes [sollten] nur nat&uuml;rliche Materialien verwendet werden. Nach einigen Versuchen erwiesen sich F&auml;den aus einer speziellen Mischung von Baumwolle und Leinen als optimal geeignet, um die Struktur- und Farbvorstellungen Hundertwassers umzusetzen. Ihre nat&uuml;rliche Variation in der Dicke verleiht dem Einband einen lebendigen Charakter; der schwarze Schu&szlig;faden und die farbigen Kettf&auml;den bilden st&auml;ndig wechselnde Farbverl&auml;ufe. (...) Durch eine besondere Beflockung mit Velours gelang es, die Linien [auf den Einb&auml;nden, Anm.] so zu gestalten, da&szlig; sie reliefartig erhaben sind. Sie gehen an den R&auml;ndern der Einb&auml;nde ineinander &uuml;ber und lassen dadurch den alles verbindenden Zusammenhang erkennen. Ein ebenfalls sehr aufwendiges Unterfangen war die Pr&auml;gung der auf Aluminium-Basis metallisierten Folien: Da es weder Werkzeuge, Stempel noch Formen gab, die Hundertwassers ausdrucksstarkem Gestaltungstil entsprachen, mu&szlig;ten sie eigens daf&uuml;r aus Messing gegossen werden. Dann erst konnte mit der eigentlichen Herstellung der Intarsien begonnen werden. Insgesamt waren &uuml;ber 50 Arbeitsg&auml;nge notwendig, um eine einzelne Buchdecke fertigzustellen. </p>
	<p>Parallel zum Entwurf der Buchdecken entwickelte Hundertwasser die Gestaltung des R&uuml;ckenschildes und der Band-Einteilungen. (...) Jeweils ein Buchstabe des Namens "Brockhaus" wurde im Fortlauf der B&auml;nde besonders herausgehoben, so da&szlig; sich aus der Entfernung wiederum aus den einzelnen hervorgehobenen Lettern der ganze Name neu ergibt. </p>
	<p class="reference">Aus der Brosch&uuml;re: Kunstwerk, Die Hundertwasser-Ausgabe der Brockhaus-Enzyklop&auml;die, o. O., 1989</p>
	</div>
	
 	<div class="newsItem">
	<p><a href="javascript:showPic(3)"><img src="images/hundertwasser/apa/apa_0195_sml.jpg" alt="" /></a></p>
	<h3>Die Bibel, 1995 </h3>
	<p> In mehrj&auml;hriger Arbeit hat Hundertwasser diese Bibelausgabe entworfen. Sie ist mit 82 Bildern illustriert, davon 32 Collagen, die er eigens f&uuml;r diese Edition geschaffen hat. Er nahm Einflu&szlig; auf die gesamte Entstehung dieser Bibel - bis hin zum Buchformat und der Art des Papiers. Der nach seinem Entwurf gewobene und beflockte Leineneinband variiert, seinem Entwurf entsprechend, bei jedem Exemplar. Jede Bibel zeichnet sich durch eine andere Farbkombination in der Leinenwebung aus. Ebenfalls unterscheiden sich die Exemplare in der Metall-Farbpr&auml;gung. Kein Exemplar gleicht dem anderen. Jeder Einband wurde &uuml;berwiegend in Handarbeit hergestellt. Um diese Einzigartigkeit zu gew&auml;hrleisten, wurden h&ouml;chste buchbinderische Herausforderungen gemeistert. </p>
	</div>

 	<div class="newsItem alternative">
	<p><a href="javascript:showPic(4)"><img src="images/hundertwasser/apa/apa_0354.jpg" alt="" /></a></p>
	<h3>Hundertwasser Catalogue Raisonn&eacute;, 1997-2002 </h3>
	<p>Einband und Buchgestaltung&nbsp;: Friedensreich Hundertwasser <br /><br />
	Text, Band I: Wieland Schmied, M&uuml;nchen <br /><br />
	Catalogue raisonn&eacute;, Band II: Andrea Christa F&uuml;rst, Wien <br /><br />
	Hundertwasser entwarf das Layout f&uuml;r das Buch ebenso wie die Ausstattung und die Einbandgestaltung: Jede Seite erhielt Hundertwassers individuelle Satzspiegelgestaltung und folgt seinen Vorstellungen zur Bildgr&ouml;&szlig;e und -anordnung. </p>
	<p>Dar&uuml;ber hinaus w&auml;hlte Hundertwasser aus seinen "Doodle"-Zeichnungen f&uuml;nf Exemplare f&uuml;r die Gestaltung der Buchkassette und der Einzelb&auml;nde aus. Weitere f&uuml;nf Hundertwasser-Doodles wurden in Farbradierungen umgesetzt, von denen jeweils eine aus einer 2000er Auflage jedem Catalogue Raisonn&eacute; beiliegt.<br/>
	Entstanden ist ein bibliophiles Werk, das Hundertwassers Schaffen genau so wiedergibt, wie er es wollte. Bis in die letzten Tage vor seiner Abreise aus Neuseeland im Februar 2000 hatte Hundertwasser Kommentare zu ausgew&auml;hlten Werken aus seinem Oeuvre verfa&szlig;t. </p>
