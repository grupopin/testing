	  <h2>Hundertwasser Architekten</h2>
  </div>
  <div style="margin: 0; padding: 0px 220px 20px 20px; ">
    <p>Freunde wie die Architekten Peter Pelikan und Heinz Springmann haben einen gro&szlig;en Anteil am Erfolg und am beispielhaften Funktionieren aller Hundertwasser-Architekturen. In ihnen hat Hundertwasser kongeniale Partner gefunden, die seine Konzepte, seine Ideen, seine Anliegen und seine Philosophie in ihrer Planung umsetzten. Hundertwasser sch&auml;tzte ihr K&ouml;nnen und ihr Wissen. <br/>
    Zu Hundertwassers "Architektur-Familie" geh&ouml;ren auch die Modellbauer, der langj&auml;hrige Freund Alfred Schmid, Andreas Bodi und ihre Teams. </p></div>
      <div style="margin: 20px 0 0 0; padding: 0px 25px 20px 120px; ">
      <h3 align="left">Dipl.-Ing. Peter Pelikan </h3>
      <p>1941 in St. P&ouml;lten, Nieder&ouml;sterreich, geboren,<br/>
      lebt in Wien<br/>
      Studium der Architektur an der Technischen Hochschule, Wien<br/>
      ab 1972 im Dienst der Stadt Wien, Magistratsabteilung 19, Stadtgestaltung<br/>
      seit 1980 architektonische Realisierung und Mitarbeit an Hundertwasser-Architekturprojekten<br/>
      Peter Pelikan ist auf dem Gebiet der Architektur der engste Mitarbeiter Hundertwassers. </p>
</div>
      <div style="margin: 20px 0 0 0; padding: 0px 220px 20px 80px; ">
      <h3>Dipl.-Ing. Heinz M. Springmann </h3>
      <p>Freier Architekt<br/>
      1951 in Rielasingen, Landkreis Konstanz, Deutschland, geboren<br/>
      Studium der Architektur an der Technischen Universit&auml;t Stuttgart<br/>
      Berufliche Schwerpunkte im Bereich von Architektur im st&auml;dtebaulichen Kontext, zeitgem&auml;&szlig;es Bauen und Denkmalschutz, seit der Zusammenarbeit mit Hundertwasser besonders Architektur und &Ouml;kologie. </p>
</div>
      <div style="margin: 20px 0 0 0; padding: 0px 25px 20px 250px; ">
      <h3>Alfred Schmid</h3>
      <p>1950 in Wien geboren,<br/>
      lebt und arbeitet in Klosterneuburg bei Wien<br/>
      Studium der Architektur an der Technischen Universit&auml;t, Wien<br/>
      seit 1975 Atelier f&uuml;r Architekturmodelle<br/>
      ab 1980 Zusammenarbeit mit Hundertwasser </p>
</div>
      <div style="margin: 20px 0 0 0; padding: 0px 220px 20px 60px; ">
      <h3>Dipl.-Ing. Andreas Bodi 
      </h3>
      <p>1963 in M&ouml;dling, Nieder&ouml;sterreich, geboren,<br/>
      lebt und arbeitet in Maria Enzersdorf bei Wien<br/>
      Studium der Geod&auml;sie an der Technischen Universit&auml;t Wien<br/>
      1984 Beginn der Modellbaut&auml;tigkeit<br/>
      ab 1986 Zusammenarbeit mit Hundertwasser<br/>
      seit 1986 Bildhauerarbeiten und seit 1993 Ausstellungen seiner Werke </p>
