  		<h2>Tapisserien und Kn&uuml;pfteppiche</h2>
      <br/>
<table  border="0">

 <tr>
  <td  valign="top">
  &nbsp;<br>  Hundertwassers erste Tapisserie <i>133  Pissender Knabe mit Wolkenkratzer</i> entstand 1952
  aufgrund einer Wette, bei der er behauptet hatte, er k&ouml;nne eine Tapisserie auch frei,
  ohne Karton, also ohne eine Vorlage in der Gr&ouml;&szlig;e der Tapisserie, weben, was ihm
  auch gelang. Auch s&auml;mtliche nachfolgende Tapisserien, die von Webern, die Hundertwasser
  ausgew&auml;hlt hatte, hergestellt wurden, entstanden ohne einen Karton.
  </td>
  <td valign="top">

  &nbsp;<br>  <a href="javascript:showPic(1);"><img src="../../../images/tapisserie/tapisserie_1.png" alt="" border="0" width="125" height="250"></a>
  </td>
 </tr>
</table>

<br><br>

<table width="100%" border="0">
 <tr>
  <td style="padding: 3px 5px 3px 5px;">
  &nbsp;<br>  <a href="javascript:showPic(2);"><img src="../../../images/tapisserie/tapisserie_2.jpg" alt="" border="0"></a>

  </td>
  <td style="padding: 7px 10px 7px 10px;">
  &nbsp;<br>  Bei der &Uuml;bertragung seiner Werke in eine Tapisserie ging es
  Hundertwasser um die freie Umsetzung eines seiner Werke in ein anderes Medium und um die
  k&uuml;nstlerische Interpretation durch die Weber, das hei&szlig;t um eine Umsetzung ohne
  Vorlage beziehungsweise Karton. Nach Hundertwassers Auffassung konnte nur diese Vorgehensweise
  ohne Karton dem Werk Leben einhauchen, nur so konnte ein echtes k&uuml;nstlerisches Werk
  entstehen und nicht eine unbeseelte Kopie der Vorlage. Aus diesem Grund sind alle Hundertwasser-Tapisserien
  Unikate.
  </td>
 </tr>
</table>

<br><br>

<table width="100%" border="0">
 <tr>
  <td style="padding: 7px 10px 7px 10px;">Anders verlief es, wenn Fremde die Initiative zur
  Herstellung von Teppichen in Kn&uuml;pftechnik ergriffen. Viele, die versucht haben,
  Hundertwasser-Kn&uuml;pfteppiche herzustellen, mu&szlig;ten ihre Werke wieder zerst&ouml;ren,
  da sie Hundertwassers Vorstellungen weder in k&uuml;nstlerischer noch in technischer
  Hinsicht gerecht werden konnten. Doch im Jahr 1998 erlaubte Hundertwasser seinem
  afghanischen Freund Zia Uddin, einen Kn&uuml;pfteppich nach dem Werk
  <i>117 Gelbe Schiffe - Das Meer von Tunis und Taormina</i> von einheimischen Kn&uuml;pferinnen
  herstellen zu lassen - nicht zuletzt zur Unterst&uuml;tzung der armen afghanischen Landbev&ouml;lkerung.
  Er war von der eigenst&auml;ndigen Interpretation so begeistert, da&szlig; er mit der Ausf&uuml;hrung
  einiger weiterer Teppiche einverstanden war. Als Hundertwasser im Februar 2000 von Neuseeland in
  Richtung Europa abreiste, nahm er den afghanischen Kn&uuml;pfteppich nach <i>117</i> mit auf
  das Schiff Queen Elizabeth 2 und legte ihn in seiner Kabine auf.
  </td>

