	  <h2>Humustoilette und Pflanzenkl&auml;ranlage </h2>

 	<div class="newsItem alternative">
    <p><a href="javascript:showPic(0)"><img src="images/hundertwasser/oeko/apa_0196_sml.jpg" width="200" height="300" alt=""></a></p>
	  <p>Seit 1975 hat sich Hundertwasser mit der Humustoilette und mit den M&ouml;glichkeiten der biologischen Wasserkl&auml;rung, vor allem mit der Pflanzenkl&auml;ranlage besch&auml;ftigt. An allen seinen Wohnsitzen hat er Humustoiletten verwendet. Er hat verschiedene Baupl&auml;ne f&uuml;r die Humustoilette entwickelt und ihre Funktion und Verwendung beschrieben. </p>
    <p>Die Humustoilette funktioniert im Haus, auch im Wohnraum. Es gibt keinen Geruch, keine Fliegen, wenn die Abf&auml;lle mit feuchtem Humus sorgf&auml;ltig abgedeckt werden. Es ist keine Kanalisation notwendig, keine Wassersp&uuml;lung, kein Abzugsrohr, es werden keine Chemikalien ben&ouml;tigt und auch kein Elektroanschlu&szlig;. </p>
  </div>

 	<div class="newsItem">
	  <p><a href="javascript:showPic(1)"><img src="images/hundertwasser/oeko/apa_0196_2_sml.jpg" width="200" height="280" alt=""></a></p>
	  <p>F&uuml;r Urin und Haushaltsabw&auml;sser hatte Hundertwasser eine biologische Kl&auml;ranlage mit Wasserpflanzen entworfen und in mehreren Ausf&uuml;hrungen realisiert, eine davon im KunstHausWien. Die Pflanzenkl&auml;ranlage kann in hellen und warmen Innenr&auml;umen verwendet werden, mit den folgenden Pflanzen: Kalla, Zyperngras, Wasserhyazinthe, Papyrus, Winterschachtelhalm, Dreimasterblume, Tropenwurz, Schneide, Kletterfeige etc. Im Freien werden f&uuml;r die Planzenkl&auml;ranlage Flechtbinse, Schilf, Rohrkolben, Wasserminze, Wasserschwertlilie u. a. verwendet.</p>
  </div>


 	<div class="newsItem alternative">
    <p><a href="javascript:showPic(2)"><img src="images/hundertwasser/oeko/apa_0196_skizze_small.jpg" width="251" height="214" alt=""></a></p>
	  <p>Das Schmutzwasser mu&szlig; durch den Wurzelbereich der Wasserpflanzen und durch die Kolonien der abbauenden Bakterien flie&szlig;en und wird auf diese Weise nat&uuml;rlich gekl&auml;rt. Der Schmutz setzt sich z. T. in Pflanzensubstanz und z. T. in mineralisierte Erde um. </p>
