	<h2>Die Spirale ist das Symbol des Lebens und des Todes</h2>
	
	<div class="newsItem alternative">
	<p><a href="javascript:showPic(0)"><img src="images/hundertwasser/malerei/0433_5c_sml.gif" alt=""></a></p>
	<p>Die Spirale liegt genau dort, wo die leblose Materie sich in Leben umwandelt. </p>
	<p>Ich bin davon &uuml;berzeugt, da&szlig; der Sch&ouml;pfungsakt sich in Spiralform vollzogen hat. </p>
	<p>Unsere Erde beschreibt den Lauf der Spirale. Wir gehen im Kreis, aber wir kommen nie wieder an den Punkt zur&uuml;ck, der Kreis schlie&szlig;t sich nicht, wir kommen nur in die N&auml;he des Punktes, wo wir gewesen sind. Das ist typisch f&uuml;r eine Spirale, da&szlig; es ein scheinbarer Kreis ist, der sich nicht schlie&szlig;t. </p>
	<p>Die echte und gerechte Spirale ist nicht geometrisch, sondern vegetativ, sie hat Ausbuchtungen, wird d&uuml;nner und dicker und flie&szlig;t um Hindernisse, die sich ihr in den Weg stellen. </p>
	</div>
	<div class="newsItem">
	<p><a href="javascript:showPic(1)"><img src="images/hundertwasser/malerei/0937_5c_sml.gif" alt=""></a></p>
	<p>Die Spirale bedeutet Leben und Tod nach allen Richtungen. Nach au&szlig;en l&auml;uft sie in die Geburt, ins Leben und anschlie&szlig;end durch ein sich scheinbar Aufl&ouml;sen ins zu Gro&szlig;e, in au&szlig;erirdische, nicht mehr me&szlig;bare Bereiche. </p>
	<p>Nach innen kondensiert sie sich durch Konzentration zum Leben und wird anschlie&szlig;end in unendlich kleinen Regionen wieder etwas, was wir als Tod bezeichnen, weil es sich unserer messenden Wahrnehmung entzieht. </p>
  </div>

