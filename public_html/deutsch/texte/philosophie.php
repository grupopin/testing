 	<div class="newsItem alternative">
 	<p><a href="javascript:showPic(1)"><img src="../../images/hundertwasser/philo/texte_cover_sml.jpg" width="400" height="264" alt=""></a></p>
 	<p>&hellip;Die Gedanken Hundertwassers formen eine Gestalt, eine Philosophie des &Auml;sthetischen &uuml;ber das Leben und die Kunst in Harmonie mit der Natur. Sie sind zumeist im Zusammenhang mit einem konkreten Anla&szlig; oder Anliegen Hundertwassers entstanden und aufgezeichnet worden, verstreut ver&ouml;ffentlicht und unterschiedlich wirksam verbreitet. Hundertwasser versteht sich dabei nicht so sehr als ein Schriftsteller, sondern vielmehr als ein Denker.</p>

 	
 	<p>Seit seiner ersten k&uuml;nstlerischen T&auml;tigkeit kreisen seine Gedanken unaufh&ouml;rlich um die gleichen Inhalte: um das Leben und die Natur und alles Sch&ouml;ne, was dazwischen, neben Zwecken und Nutzen, sich abspielt. Es f&auml;llt dabei auf, wie sie sich zunehmend feiner aufgliedern, gezielter ausrichten und in die Tiefe reichen. Sie gehorchen nicht den flachen Moden, dem L&auml;rmen in der &Ouml;ffentlichkeit und den Tauschwerten, sie verdanken ihre Entstehung den unabl&auml;ssigen und genauen Beobachtungen der Menschen in ihren vielf&auml;ltigen Verflechtungen und Abh&auml;ngigkeiten, Notwendigkeiten und Gelegenheiten, aber auch in ihren Verfehlungen&hellip;</p>
 	
	<p class="reference">Walter Schurian, in:<br/>
	<cite>Friedensreich Hundertwasser, Sch&ouml;ne Wege, Gedanken &uuml;ber Kunst und Leben<br/>

	Schriften 1943 - 1999</cite>, M&uuml;nchen 2004 </p>
