	  	<h2>Verschimmelungsmanifest gegen den Rationalismus in der Architektur</h2>
	  	 <p class="reference">(1958/1959/1964)</p>
	  	 
	  	 <br><br>
	
	 	<div class="newsItem alternative">
	 	
	<p><a href="javascript:showPic(0)"><img src="images/hundertwasser/philo/verschimmelungs_1958seckau_sml.jpg" width="410" height="283" alt=""></a></p>

 	    <p>Die Malerei und die Skulptur sind jetzt frei, denn jedermann darf heute allerlei Gebilde produzieren und nachher ausstellen. In der Architektur besteht jedoch diese grunds&auml;tzliche Freiheit, die als Bedingung jeder Kunst anzusehen ist, noch immer nicht, denn man mu&szlig; erst ein Diplom haben, um bauen zu k&ouml;nnen. Warum?</p>
 	    <p>Jeder soll bauen k&ouml;nnen, und solange diese Baufreiheit nicht existiert, kann man die gegenw&auml;rtige, geplante Architektur &uuml;berhaupt nicht zur Kunst rechnen. Die Architektur unterliegt bei uns derselben Zensur wie die Malerei in der Sowjetunion. Was realisiert ist, sind einzeln dastehende erb&auml;rmliche Kompromisse von Linealmenschen mit schlechtem Gewissen!</p>

		<p>Man soll den Baugel&uuml;sten des einzelnen keine Hemmungen auferlegen! Jeder soll bauen k&ouml;nnen und bauen m&uuml;ssen und so die wirkliche Verantwortung tragen f&uuml;r die vier W&auml;nde, in denen er wohnt. Und man mu&szlig; das Risiko mit in Kauf nehmen, da&szlig; so ein tolles Gebilde nachher zusammenf&auml;llt, und man soll und darf sich vor Menschenopfern nicht scheuen, die diese neue Bauweise erfordert, vielleicht erfordert. Es mu&szlig; endlich aufh&ouml;ren, da&szlig; Menschen ihr Quartier beziehen wie die Hendeln und die Kaninchen ihren Stall.</p>
    <p>Wenn so ein von den Bewohnern selbstgebautes, tolles Gebilde zusammenf&auml;llt, so kracht es ja meistens ohnehin vorher, so da&szlig; man fl&uuml;chten kann. Der Mieter wird jedoch von da an kritischer und sch&ouml;pferischer den Geh&auml;usen gegen&uuml;berstehen, die er bewohnt, und wird mit den eigenen H&auml;nden die W&auml;nde und Pfeiler verdicken, falls sie ihm zu zerbrechlich scheinen.</p>
  </div>
  
<div class="newsItem alternative">
  	  <p><b>+</b> Die materielle Unbewohnbarkeit der Elendsviertel ist der moralischen Unbewohnbarkeit der funktionellen, n&uuml;tzlichen Architektur vorzuziehen. In den sogenannten Elendsvierteln kann nur der K&ouml;rper des Menschen zugrundegehen, doch in der angeblich f&uuml;r den Menschen geplanten Architektur geht seine Seele zugrunde. Daher ist das Prinzip der Elendsviertel, das hei&szlig;t der wild wuchernden Architektur, zu verbessern und als Ausgangsbasis zu nehmen und nicht die funktionelle Architektur. <b>+</b></p>
  	  <p>Die funktionelle Architektur hat sich als Irrweg erwiesen, genauso wie die Malerei mit dem Lineal. Wir n&auml;hern uns mit Riesenschritten der unpraktischen, der unnutzbaren und schlie&szlig;lich der unbewohnbaren Architektur.</p>
  	  <p>Die gro&szlig;e Wende, f&uuml;r die Malerei der absolute tachistische Automatismus, ist f&uuml;r die Architektur die absolute Unbewohnbarkeit, die uns noch bevorsteht, da die Architektur um drei&szlig;ig Jahre nachhinkt.</p>
  	  <p>So wie wir heute schon, nach &Uuml;berschreitung des totalen tachistischen Automatismus, die Wunder des Transautomatismus erleben, so werden wir erst nach &Uuml;berwindung der totalen Unbewohnbarkeit und der sch&ouml;pferischen Verschimmelung die Wunder einer neuen, wahren und freien Architektur erleben. Da wir jedoch die totale Unbewohnbarkeit noch nicht hinter uns haben, da wir uns leider noch nicht im Transautomatismus der Architektur befinden, m&uuml;ssen wir vorerst einmal die totale Unbewohnbarkeit, die sch&ouml;pferische Verschimmelung in der Architektur so rasch wie m&ouml;glich anstreben.</p>
  	  </div>

<div align="center">
	<p><a href="javascript:showPic(1)"><img src="images/hundertwasser/philo/verschim_manifest_4_small.jpg" width="350" height="267" alt=""></a></p>
</div>
<div class="newsItem">
	<p>Ein Mann in einem Mietshaus mu&szlig; die M&ouml;glichkeit haben, sich aus seinem Fenster zu beugen und - so weit seine H&auml;nde reichen - das Mauerwerk abzukratzen. Und es mu&szlig; ihm gestattet sein, mit einem langen Pinsel - so weit er reichen kann - alles rosa zu bemalen, so da&szlig; man von weitem, von der Stra&szlig;e, sehen kann: Dort wohnt ein Mensch, der sich von seinen Nachbarn unterscheidet, dem zugewiesenen Kleinvieh! Auch mu&szlig; er die Mauern zers&auml;gen und allerlei Ver&auml;nderungen vornehmen k&ouml;nnen, auch wenn dadurch das architektonisch-harmonische Bild eines sogenannten Meisterwerkes der Architektur gest&ouml;rt wird, und er mu&szlig; sein Zimmer mit Schlamm oder Plastilin anf&uuml;llen k&ouml;nnen.</p>
  	  <p>Doch im Mietvertrag ist dies verboten!</p>
  	  <p>Es ist an der Zeit, da&szlig; die Leute selbst dagegen revoltieren, da&szlig; man sie in Schachtelkonstruktionen setzt, so wie die Hendeln und die Hasen in K&auml;figkonstruktionen, die ihnen wesensfremd sind.</p>
  	
    	<p><a href="javascript:showPic(2)"><img src="images/hundertwasser/philo/verschim_manifest_2_small.jpg" width="241" height="350" alt=""></a></p>

      <p><b>+</b> Eine K&auml;figkonstruktion oder N&uuml;tzlichkeitskonstruktion ist ein Geb&auml;ude, das allen drei Kategorien von Menschen wesensfremd bleibt, die damit zu tun haben! </p>
  	  <ul>
  	    <li>Der Architekt hat keinerlei Beziehung zum Gebilde. Selbst wenn er das gr&ouml;&szlig;te Architekturgenie ist, so kann er doch nicht voraussehen, welcher Art der Mensch ist, der darin wohnen wird. Das sogenannte menschliche Ma&szlig; in der Architektur ist ein verbrecherischer Betrug. Besonders dann, wenn dieses Ma&szlig; als Mittelwert aus einem Gallupsystem hervorgegangen ist.</li>
        <li>Der Maurer hat keinerlei Beziehung zum Gebilde. Wenn er beispielsweise eine Mauer nur etwas anders, nach seinen pers&ouml;nlichen Auffassungen, falls er welche hat, gestalten will, so verliert er seine Arbeit. Und au&szlig;erdem ist es ihm ja ganz egal, da er ja nicht in dem Gebilde wohnen wird.</li>
  	    <li>Der Bewohner hat keinerlei Beziehung zum Gebilde, weil er es ja nicht gebaut hat und er nur eingezogen ist. Seine menschlichen Notwendigkeiten, sein menschlicher Raum ist sicherlich ein ganz anderer. Und dies bleibt aufrecht, selbst wenn sich Architekt und Maurer bem&uuml;hen, genau nach den Angaben des Bewohners und Bestellers zu bauen.  <b>+</b></li>
      </ul>
  	  <p>
  	    <b>+</b> Nur wenn Architekt, Maurer und Bewohner eine Einheit sind, das hei&szlig;t eine und dieselbe Person, kann man von Architektur sprechen. Alles andere ist keine Architektur, sondern eine verbrecherische, gestaltgewordene Tat.
      </p>
</div>

<div class="newsItem">
	  <p>&nbsp;</p>

  	  <p>Architekt-Maurer-Bewohner sind eine Dreieinigkeit genau wie Gottvater-Sohn-Heiliger-Geist. Man beachte die &Auml;hnlichkeit, quasi Identit&auml;t der Dreieinigkeiten. Geht die Einheit Architekt-Maurer-Bewohner verloren, so gibt es keine Architektur, so wie die jetzt fabrizierten Gebilde nicht als Architektur anzusehen sind. Der Mensch mu&szlig; seine kritisch-sch&ouml;pferische Funktion wiedereinnehmen, die er verloren hat und ohne die er aufh&ouml;rt, als Mensch zu existieren! <b>+</b></p>
  	
    	<p><a href="javascript:showPic(3)"><img src="images/hundertwasser/philo/verschim_manifest_1_small.jpg" width="217" height="350" alt=""></a></p>

      <p><b>+</b> Verbrecherisch ist ferner die Benutzung des Lineals in der Architektur, das, wie leicht zu beweisen ist, als Instrument des Zerfalls der architektonischen Dreieinigkeit anzusehen ist. <b>+</b></p>
  	  <p>Schon das Bei-sich-Tragen einer geraden Linie m&uuml;&szlig;te, zumindest moralisch, verboten werden. </p>
  	  <p>Das Lineal ist das Symbol des neuen Analphabetentums. Das Lineal ist das Symptom der neuen Krankheit des Zerfalls.</p>
  </div>

<div class="newsItem alternative">
	<p>Wir leben heute in einem Chaos der geraden Linien, in einem Dschungel der geraden Linien. Wer dies nicht glaubt, der gebe sich einmal die M&uuml;he und z&auml;hle die geraden Linien, die ihn umgeben, und er wird begreifen; denn er wird niemals ans Ende gelangen.</p>
  	  <p>Auf einer Rasierklinge habe ich 546 gerade Linien gez&auml;hlt. Durch die lineare und imagin&auml;re Verbindung zu einer zweiten Rasierklinge derselben Produktion, die sicher haargenau so aussieht, ergeben sich 1090  gerade Linien und, wenn man die Verpackung dazuz&auml;hlt, an die 3000 geraden Linien derselben Rasierklinge.</p>
  	  <p>Vor nicht allzulanger Zeit war der Besitz der geraden Linien ein Privileg der K&ouml;nige, der Beg&uuml;terten und der Gescheiten. Heute besitzt jeder Depp Millionen von geraden Linien in der Hosentasche.</p> </div>
  	  
<div class="newsItem">
  	  <p>Dieser Urwald der geraden Linien, der uns immer mehr wie Gefangene in einem Gef&auml;ngnis umstrickt, mu&szlig; gerodet werden.</p>
		  <p>Der Mensch hat bisher immer noch die Dschungel gerodet, in denen er sich befand, und sich befreit. Allerdings mu&szlig; er sich erst dessen bewu&szlig;t werden, da&szlig; er sich in einem Dschungel befindet, denn dieser Dschungel hat sich schleicherisch gebildet, ohne da&szlig; die Bev&ouml;lkerung davon etwas wei&szlig;. Und diesmal ist es ein Dschungel der geraden Linien.</p>
  	  <p>Jede moderne Architektur, bei der das Lineal oder der Zirkel auch nur eine Sekunde lang - und wenn auch nur in Gedanken - eine Rolle gespielt hat, ist zu verwerfen. Gar nicht zu reden von der Entwurf-, Rei&szlig;brett- und Modellarbeit, die nicht nur krankhaft steril, sondern wahrhaft widersinnig geworden ist. Die gerade Linie ist gottlos und unmoralisch. Die gerade Linie ist keine sch&ouml;pferische, sondern eine reproduktive Linie. In ihr wohnt weniger Gott und menschlicher Geist als vielmehr die bequemheitsl&uuml;sterne, gehirnlose Massenameise.</p>
  </div>
  
	<div class="newsItem alternative">
      <p>Die Gebilde der geraden Linie, auch wenn sie sich noch so kr&uuml;mmen, biegen, &uuml;berh&auml;ngen und sogar durchl&ouml;chern, sind somit hinf&auml;llig. Das ist alles Anschlu&szlig;panik, ist die Angst der konstruktiven Architekten, nur ja rechtzeitig umzuwechseln zum Tachismus, das hei&szlig;t zur Unbewohnbarkeit.</p>
  	  <p>Wenn sich an einer Rasierklinge der Rost festsetzt, wenn eine Wand zu schimmeln beginnt, wenn in einer Zimmerecke das Moos w&auml;chst und die geometrischen Winkel abrundet, so soll man sich doch freuen, da&szlig; mit den Mikroben und Schw&auml;mmen das Leben in das Haus einzieht und wir so mehr bewu&szlig;t als jemals zuvor Zeugen von architektonischen Ver&auml;nderungen werden, von denen wir viel zu lernen haben.</p>
  	  <p><a href="javascript:void(0);"><img src="images/hundertwasser/philo/verschimmelungs_cover_sml.jpg" width="226" height="320" alt="" /></a>Die verantwortungslose Zerst&ouml;rungswut der konstruktiven funktionellen Architekten ist bekannt. Sie wollten die sch&ouml;nen H&auml;user mit Stuckfassaden der neunziger Jahre und des Jugendstils einfach abrei&szlig;en und ihre leeren Gebilde hinpflanzen. Ich weise auf Le Corbusier, der Paris dem Erdboden gleichmachen wollte, um geradlinige Monsterkonstruktionen hinzusetzen. Um Gerechtigkeit  zu &uuml;ben, m&uuml;&szlig;te man jetzt die Gebilde von Mies van der Rohe, Neutra, Bauhaus, Gropius, Johnson, Le Corbusier usw. auch abrei&szlig;en, da sie seit einer Generation veraltet und moralisch unertr&auml;glich geworden sind.</p>
	    <p>Die Transautomatisten und alle, die sich jenseits der unbewohnbaren Architektur befinden, verfahren jedoch humaner mit ihren Vorg&auml;ngern. Sie wollen nicht mehr zerst&ouml;ren.</p>
  	  <p>Um die funktionelle Architektur vor dem moralischen Ruin zu retten, soll man auf die sauberen Glasw&auml;nde und Betongl&auml;tten ein Zersetzungsprodukt gie&szlig;en, damit sich dort der Schimmelpilz festsetzen kann.</p>
  	  <p><b>++</b> Es ist an der Zeit, da&szlig; die Industrie ihre fundamentale Mission erkennt, und die ist: sch&ouml;pferische Verschimmelung betreiben!</p>
  	  <p>Es ist jetzt Aufgabe der Industrie, bei ihren Spezialisten, Ingenieuren und Doktoren ein moralisches Verantwortungsgef&uuml;hl gegen&uuml;ber der Verschimmelung hervorzubringen. Dieses moralische Verantwortungsgef&uuml;hl gegen&uuml;ber der sch&ouml;pferischen Verschimmelung und der kritischen Verwitterung mu&szlig; schon im Erziehungsgesetz verankert sein.</p>
  	  <p>Nur die Techniker und Wissenschaftler, die imstande sind, im Schimmel zu leben und Schimmel sch&ouml;pferisch zu erzeugen, werden die Herren von morgen sein.  <b>++</b></p>
  	  <p>Und erst nach der sch&ouml;pferischen Verschimmelung, von der wir viel zu lernen haben, wird eine neue und wunderbare Architektur entstehen. <b>+</b></p>
  	  <p><i>Die mit + eingefa&szlig;ten Zus&auml;tze sind erst nach dem Vortrag auf der Seckauer Tagung dem Verschimmelungsmanifest hinzugef&uuml;gt worden.</i></p>
  	  <p><i>++ Diese Abs&auml;tze sind eine Einf&uuml;gung von Pierre Restany, 1958</i></p>
  </div>
  	  
<div class="newsItem alternative">
	<p>&nbsp;</p>
  	  <h3>Zusatz 1959</h3>
  	  <p>Die heutige Architektur ist kriminell steril. Denn fatalerweise h&ouml;rt jegliche Baut&auml;tigkeit in dem Augenblick auf, in dem Menschen "ihr Quartier beziehen", wo doch normalerweise die Baut&auml;tigkeit nach Einzug des Menschen &uuml;berhaupt erst beginnen sollte.</p>
  	  <p>Wir werden so unerh&ouml;rterweise durch Schanddiktate unserer Menschlichkeit beraubt und auf verbrecherische Art gezwungen, nichts an der Fassade, Anlage, an den Innenr&auml;umen weder in Farbe noch Struktur oder Mauerwerk zu &auml;ndern oder hinzuzuf&uuml;gen. Selbst Eigenbesitzwohnungen unterliegen der Zensur (siehe Verordnungen der Baubeh&ouml;rde und Statuten des Mietvertrages).</p>
      <p>Das Charakteristische eines Gef&auml;ngnisses, K&auml;figs oder Stalles ist eben der a priori fertiggestellte Bau, der endg&uuml;ltige Abbruch der Baut&auml;tigkeit vor Einzug des Tieres oder des Gefangenen in f&uuml;r ihn wesensfremde Gebilde, verbunden mit dem kategorischen Zwang f&uuml;r den Insassen, an "seinem" aufoktroyierten Geh&auml;use nichts zu &auml;ndern. Da&szlig; der Mensch dann zwischendurch aus dem Gef&auml;ngnis herauskommt und "frei" in der Stadt herumgehen kann, &auml;ndert nichts an der Sache.</p>

    	<p><a href="javascript:showPic(4)"><img src="images/hundertwasser/philo/verschim_manifest_3_small.jpg" width="251" height="192" alt=""></a></p>

      <p>Denn die wahrhafte Architektur entsteht durch normale Baut&auml;tigkeit und diese normale Baut&auml;tigkeit ist das organische Wachstum einer H&uuml;lle um eine Gruppe von Menschen. Dieses Bauwachstum verh&auml;lt sich genau so wie das Wachstum des Kindes und des Menschen.</p>
      <p>Der absolute Schlu&szlig;strich unter die Baut&auml;tigkeit eines Gebildes ist nur bei Monumenten und unbewohnbaren Architekturen unbedingt tragbar.</p>
  	  <p>Falls das Gebilde jedoch dazu bestimmt ist, Menschen in seinem Inneren zu beherbergen, so ist der absolute Abbruch der Baut&auml;tigkeit vor Einzug des Menschen als widernat&uuml;rliche Sterilisierung des Wachstums und somit als kriminelles Vergehen zu betrachten und zu ahnden.</p>
  	  <p>Der Architekt, wie wir ihn heute kennen, hat nur das Recht, unbewohnbare Architekturen zu bauen, falls er &uuml;berhaupt dazu f&auml;hig ist. Bewohnbare Architekturen zu bauen, unterliegt nicht seiner Kompetenz und dies mu&szlig; ihm entschieden verwehrt werden; wie man auch notorische Giftmischer und Massenausrotter nicht frei hantieren l&auml;&szlig;t.</p>
  	  <p>Denn die jetzt vielgepriesene architektonische Vorplanung von Wohnst&auml;tten ist nichts anderes als gelenkter Massenmord durch vors&auml;tzliche Sterilisierung. Es gen&uuml;gt ein Gang durch eine europ&auml;ische Stadt, besonders aber durch ein neuerbautes Stadtviertel, um diese ersch&uuml;tterne Anklage f&uuml;r jedermann beweiskr&auml;ftig zu machen.</p>
  	  <p>Nur eine Idee von beispielgebenden gesunden Architekturen der Jetztzeit, und diese Liste ist leider besch&auml;mend klein:</p>
  	  <ol>
  	    <li>1. Geb&auml;ude von Gaud&iacute; in Barcelona</li>
        <li>2. Gewisse Gebilde des Jugendstils</li>
  	    <li>3. The Tower of Watts von Sam Rodia in einem Vorort von Los Angeles</li>
  	    <li>4. Le Palais Du Facteur Cheval im Departement de la Dr&ocirc;me, Frankreich</li>
  	    <li>5. Die Elendsviertel, die sogenannten "Schandflecke" jeder Stadt (Slums, Taudis, quartiers insalubres)</li>
  	    <li>6. Die Bauernh&auml;user und H&auml;user der Primitiven, falls sie jetzt noch wie fr&uuml;her mit den H&auml;nden geformt werden</li>
  	    <li>7. Die Schrebergartenh&auml;user der Arbeiter</li>
  	    <li>8. <i>Illegal, von den Bewohnern selbst errichtete H&auml;user in Amerika.</i>*</li>
  	    <li>9. Holl&auml;ndische Hausboote, <i>Sausalito-Hausboote.</i>*</li>
  	    <li>10. Bauten der Architekten Christian Hunziker, <i>Lucien Kroll</i>* und weniger anderer.</li>
