  <div id="content" class="wide">
  		<h2>Hundertwasser &uuml;ber Hundertwasser</h2>
  		
		<div class="newsItem"> 
		<p><a href="javascript:showPic(0);"><img src="/images/hundertwasser/fruh-p1.jpg" width="92" height="104" alt=""></a></p>
 		<p style="padding: 5px;">Was braucht ein Mensch, um gl&uuml;cklich zu sein</p>
    	<p style="padding: 5px;">Fortschritt ist R&uuml;ckschritt, und der R&uuml;ckschritt wird zum Fortschritt</p>

    	<p style="padding: 5px;">Meine Malerei ist, glaube ich, deshalb v&ouml;llig anders, weil es eine vegetative Malerei ist</p>
  		<p style="padding: 5px;">Ein Grund, warum die anderen Leute nicht vegetativ malen wollen oder eine vegetative
      Lebenshaltung einschlagen wollen, ist, weil sie zu unscheinbar beginnt, weil sie keinen
      Eclat hat und keinen Paukenschlag, sondern weil sie ganz langsam und unscheinbar eben
      w&auml;chst, und das entspricht nicht unserer Gesellschaftsordnung, man will sofortige Leistung,
      die auf Raubbau beruht</p>
    	<p style="padding: 5px;">Ich m&ouml;chte, das mache ich auch ganz instinktiv, vorleben, den Menschen vorleben,
      vormalen ein Paradies, das jeder haben kann, er braucht nur zuzugreifen</p>
      <p style="padding: 5px;">Das Paradies ist ja da, wir machen es nur kaputt</p>
    	<p><a href="javascript:showPic(1);"><img src="/images/hundertwasser/fruh-p2.jpg" width="91" height="113" alt=""></a></p>

  		<p style="padding: 5px;">Ich will zeigen, wie einfach es im Grunde ist, das Paradies auf Erden zu haben</p>
  		<p style="padding: 5px;">Und alles das, was die Religionen und die Dogmen und die verschiedenen politischen Richtungen versprechen, das ist alles Nonsens</p>
 	</div>
	
		<div class="newsItem"> 
		<p style="padding: 5px;">Und da komme ich nat&uuml;rlich in Konflikt mit der Gesellschaft, die das v&ouml;llig mi&szlig;versteht</p>
  	<p style="padding: 5px;">Die glauben, da&szlig; das eine Exzentrik ist, nur um Publicity zu machen, dabei vergessen sie, da&szlig; das ein Teil meiner selbst ist, da&szlig; das meine nat&uuml;rliche Ausdrucksform ist</p>

  	<p style="padding: 5px;">Warum darf ein Mensch, so wie eine Blume, nicht das tun, was ihm entspricht</p>
  	<p style="padding: 5px;">Das Bunte, das Vielf&auml;ltige, das Mannigfaltige, ist auf alle F&auml;lle besser als das Graue, das Durchschnittsgrau</p>
  	<p style="padding: 5px;">Nur wer sch&ouml;pferisch denkt und lebt, wird &uuml;berleben im Diesseits und im Jenseits.</p>
	</div>
	
		<div class="newsItem alternative"> 
		<p><a href="javascript:showPic(2);"><img src="/images/hundertwasser/fruh-p3.jpg" width="94" height="120" alt=""></a></p>

		<p style="padding: 5px;">Man mu&szlig; leben, als w&auml;re man im Krieg, wo alles rationiert ist</p>
		<p style="padding: 5px;">Der Mensch mu&szlig; vorsichtig sein</p>
		<p style="padding: 5px;">Mu&szlig; selbst&auml;ndig denken, mu&szlig; haushalten</p>

		<p style="padding: 5px;">Darf nicht blind verschwenden</p>
		<p style="padding: 5px;">Der Mensch mu&szlig; achten, da&szlig; der Kreislauf funktioniert</p>
		<p style="padding: 5px;">Der Kreislauf vom Essen zur Schei&szlig;e funktioniert nat&uuml;rlich</p>
		<p style="padding: 5px;">Aber der Kreislauf von der Schei&szlig;e zum Essen ist unterbrochen</p>

		<p style="padding: 5px;">Das Gl&uuml;cklichsein h&auml;ngt von Reichtum &uuml;berhaupt nicht ab</p>
		<p style="padding: 5px;">H&auml;ngt von der Produktion nicht ab</p>
		<p style="padding: 5px;">Das ist schwer zu sagen.</p>
	</div>
	
		<div class="newsItem"> 
    <p><a href="javascript:showPic(3);"><img src="/images/hundertwasser/fruh-p4.jpg" width="96" height="103" alt=""></a></p>

		<p style="padding: 5px;">Die Bilder sind f&uuml;r mich Tore, die es mir erm&ouml;glichen, wo es mir
    gelungen ist, sie aufzusto&szlig;en in eine Welt, die uns gleichzeitig
    sehr nah und sehr fern ist, wo wir keinen Zutritt haben, in der
    wir uns befinden, aber die wir nicht wahrnehmen k&ouml;nnen, die
    gegen die tats&auml;chliche Welt ist
    </p>
    <p style="padding: 5px;">Unsere Parallelwelt, von der wir uns einerseits entfernen</p>
    <p style="padding: 5px;">Ja, und das ist das Paradies, das ist das, worin wir sind, worin wir verhaftet sind
    und was uns irgendeine unerkl&auml;rliche Macht versagt</p>
		<p style="padding: 5px;">Da ist es mir gelungen, Fenster dazu aufzusto&szlig;en</p>

		<p style="padding: 5px;">Wie es mir gelungen ist, ist auch schwierig zu erkl&auml;ren</p>
		<p style="padding: 5px;">Auf gar keinen Fall mit Gewalt, auch nicht mit &Uuml;berlegung,
    auch nicht mit Intelligenz, auch nicht unbedingt mit Intuition,
    sondern fast wie traumwandlerisch</p>
	</div>
	
		<div class="newsItem alternative"> 
		<p><a href="javascript:showPic(4)"><img src="/images/hundertwasser/reife-p1.jpg" width="88" height="95" alt=""></a></p>
		<p style="padding: 5px;">Die Arbeit des K&uuml;nstlers ist eben sehr schwierig, weil sie mit Gewalt,
    mit Flei&szlig; und mit Intelligenz nicht getan werden kann</p>

    <p style="padding: 5px;">Ich meine, mit Kraft und Flei&szlig; und Intelligenz kann man
    sonst im Leben alles tun, doch mit diesen Dingen bleibt
    einem das Resultat in der Kunst total versperrt</p>
    <p style="padding: 5px;">Also auch mit G&uuml;te, auch ein guter Mensch sieht sich
    pl&ouml;tzlich vor einer Barriere, also er kann nicht weiter</p>
    <p style="padding: 5px;">Es ist etwas sehr Seltsames, nicht wahr, wenn ein Mensch
    alles mitbringt, was er hat, Flei&szlig;, G&uuml;te, Ausdauer,
    Intelligenz, alles, was der Mensch eben bekommen kann,
    und trotzdem kann er nicht heran</p>
    <p style="padding: 5px;">Woran liegt es</p>

    <p style="padding: 5px;">Ich glaube, und ich bin absolut sicher, deswegen glaube ich,
    da&szlig; Malerei eine religi&ouml;se Besch&auml;ftigung ist,<br>
    da&szlig; dann der tats&auml;chliche Impuls von au&szlig;en kommt, von
    irgendetwas anderem, was wir nicht kennen, eine undefinierbare Macht,
    die kommt oder nicht kommt und die einem die Hand f&uuml;hrt</p>
    
    <p style="padding: 5px;">Man hat fr&uuml;her gesagt, es w&auml;re die Muse zum Beispiel; es ist
    ein bl&ouml;des Wort nat&uuml;rlich, aber es ist irgendeine Erleuchtung</p>

    
    <p style="padding: 5px;">Und das einzige, was man tun kann, ist das Terrain vorzubereiten,
    da&szlig; diese au&szlig;erirdischen, oder wie man sie sonst
    nennen kann, Impulse einen erreichen k&ouml;nnen</p>
    </div>
		<div class="newsItem">
    <p style="padding: 5px;"><a href="javascript:showPic(5);"><img src="/images/hundertwasser/reife-p3.jpg" width="102" height="107" alt=""></a></p>
    <p style="padding: 5px;">Das hei&szlig;t, sich bereithalten</p>

    
    <p>Das hei&szlig;t, den Willen ausschalten, die Intelligenz ausschalten, das
    "Besser-machen-Wollen" ausschalten, die Strebsucht ausschalten</p>
    
    <p style="padding: 5px;">Ich m&ouml;chte vielleicht bezeichnet werden als Magier der Vegetation oder so etwas
    &auml;hnliches, also Magie, da&szlig; ich eben ein Bild anf&uuml;lle, bis es voll ist mit Magie,
    wie wenn man ein Glas anf&uuml;llt mit Wasser</p>
    
    <p style="padding: 5px;">Alles ist so unendlich einfach, so unendlich sch&ouml;n</p>

    
    <p style="padding: 5px; font-style: italic;">Venedig, 1975</p>
	</div>
	
