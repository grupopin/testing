<h2>Biographie - Eine Auswahl</h2>

	<div style="width: 640px;">
  		<table border="0" style="border: 0px solid white;">
  		
        <tr>

          <td class="year">1928</td>
          <td>Am 15. Dezember in Wien geboren als Friedrich Stowasser.</td>
          <td rowspan="17" style="width: 15px;"></td>
          <td rowspan="17">
          &nbsp;<br/>



<a href="javascript:showPic(20)"><img src="/images/hundertwasser/bio/kindheit_mitmutter_small.jpg" alt="" /></a>
<div style="font-size:11px; padding: 5px; width: 240px;">Friedrich Stowasser mit seiner Mutter am Meer, ca. 1932<br/>Foto: Hundertwasser Archiv</div>

<div style="height:55px;"></div>

<a href="javascript:showPic(21)"><img src="/images/hundertwasser/bio/1950_pavillon_castiglione_small.jpg" alt="" /></a>
<div style="font-size:11px; padding: 5px; width: 240px;">Hundertwasser mit Ren&eacute; Br&ocirc; im Pavillon Castiglione in St. Mand&eacute; bei Paris vor dem gemeinsam gemalten Wandbild, 1950<br/>Foto: Hundertwasser Archiv</div>

<div style="height:55px;"></div>

<a href="javascript:showPic(22)"><img src="/images/hundertwasser/bio/1954_paris_small.jpg" alt="" /></a>

<div style="font-size:11px; padding: 5px; width: 240px;">Hundertwasser vor der Er&ouml;ffnung seiner Ausstellung im Studio Paul Facchetti, Paris, 1954<br/>Foto: Hundertwasser Archiv</div>



          </td>
        </tr>

        <tr valign="top">
          <td class="year">1929</td>

          <td>Tod des Vaters, technischer Beamter und Offizier im 1. Weltkrieg.</td>
        </tr>
        
        <tr valign="top">
          <td class="year">1934</td>
          <td valign="top">Erste Jugendzeichnungen.</td>
        </tr>

        <tr valign="top">

          <td class="year">1936</td>
          <td>Ein Jahr Besuch der Montessori Schule in Wien. Im Zeugnis wird der
          "au&szlig;ergew&ouml;hnliche Farben- und Formensinn" betont.</td>
        </tr>
        
        <tr valign="top">
          <td class="year">1943</td>
          <td>Erste bewu&szlig;te Buntstiftzeichnungen nach der Natur. In diesem Jahr werden 69
          j&uuml;dische Familienangeh&ouml;rige m&uuml;tterlicherseits deportiert und get&ouml;tet, auch Tante
          und Gro&szlig;mutter. Erstes Tagebuch 1943-1948.
          </td>

        </tr>

        <tr valign="top">
          <td class="year">1948</td>
          <td>Matura. Verbringt drei Monate an der Akademie der Bildenden K&uuml;nste bei Prof.
          Robin Christian Andersen. Eine Walter Kampmann-Ausstellung in der Albertina und
          Schiele-Ausstellungen hinterlassen nachhaltigen Eindruck.
          Vortrag auf Schlo&szlig; Leopoldskron: <i>Everybody must be creative.</i></td>
        </tr>

        <tr valign="top">
          <td class="year">1949</td>
          <td>Beginnt ausgedehnte Reisen: Norditalien, Toskana, Rom, Neapel, Sizilien. In
          Florenz trifft er Ren&eacute; Br&ocirc; und folgt ihm nach Paris. Entwickelt eigenen Stil.
          Nimmt den Namen Hundertwasser an.</td>
        </tr>
        
        <tr valign="top">

          <td class="year">1950</td>
          <td>Aufenthalt in Paris bei Br&ocirc; und Familie Dumage. Malt gemeinsam mit Br&ocirc; zwei
          Wandbilder in Saint Mand&eacute;. Verl&auml;&szlig;t die Ecole des Beaux Arts am ersten Tag.
          Begegnung mit Shinkichi Tajiri.</td>
        </tr>

        <tr valign="top">

          <td class="year">1951</td>
          <td>Verbringt Winter und Fr&uuml;hjahr in Marokko und Tunesien. Wird Mitglied des Art
          Club Wien.</td>
        </tr>

        <tr valign="top">
          <td class="year">1952</td>
          <td>Erste Ausstellung im Art Club Wien. Kurze dekorativ-abstrakte Periode.
          </td>

        </tr>
        
        <tr valign="top">
          <td class="year">1953</td>
          <td>Malt die erste Spirale.
          Zweiter Aufenthalt in Paris. Arbeitet in Br&ocirc;s Studio in St. Maurice.
          Zweite Ausstellung im Art Club Wien.
          </td>
        </tr>
        
        <tr valign="top">
          <td class="year">1954</td>

          <td>Erste Ausstellung in Paris bei Paul Facchetti.
          Verbringt September und Oktober im Spital Santo Spirito in Rom mit Gelbsucht,
          malt viele Aquarelle. Entwickelt die Theorie des "Transautomatismus" und beginnt
          seine Bilder zu numerieren.
          </td>
        </tr>
        
        <tr valign="top">
          <td class="year">1955</td>
          <td>Ausstellung bei Carlo Cardazzo, Galleria del Naviglio, Mailand.
          </td>
        </tr>

        <tr valign="top">

          <td class="year">1956</td>
          <td>Zweite Ausstellung bei Facchetti, Paris.
          Im Sommer segelt er als Matrose zusammen mit Hans Neuffer auf der S/S Bauta von S&ouml;derham nach Hull.
          Ver&ouml;ffentlicht den Text <i>La visibilit&eacute; de la cr&eacute;ation transautomatique</i> in "Cimaises"
          und <i>Cin&eacute;ma individuel transautomatique</i> in "Phases" in Paris.
          Freundschaft mit dem Sammler Siegfried Poppe.
          </td>

        </tr>

